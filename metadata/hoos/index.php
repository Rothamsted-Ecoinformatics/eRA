<h1>Hoosfield Spring Barley Experiment</h1>
<p>
	The <B>Hoosfield spring barley experiment</B> was started by Lawes and
	Gilbert in 1852. Spring barley has been grown continuously since then.
	The experiment offers interesting contrasts to Broadbalk; because it is
	spring sown it has only been necessary to fallow it four times to
	control weeds and it tests not only nitrogen, minerals and FYM but also
	sodium silicate.
</p>

