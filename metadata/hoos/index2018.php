								<h1>Hoosfield Spring Barley Experiment</h1>
								<p>
									The <B>Hoosfield spring barley experiment</B> was started by
									Lawes and Gilbert in 1852. Spring barley has been grown
									continuously since then. The experiment offers interesting
									contrasts to Broadbalk; because it is spring sown it has only
									been necessary to fallow it four times to control weeds and it
									tests not only nitrogen, minerals and FYM but also sodium
									silicate.
								</p>
								<p>The aim was to test the effects of different inorganic
									fertilizers (supplying the elements N, P, K Mg and sodium
									silicate) and farmyard manure (FYM) on the yield of spring
									barley. The experiment has had 4 main phases:</p>
							</div>
							<div class="tab-pane" id="background" role="tabpanel"
								aria-labelledby="background-tab">
								<h2>Timeline</H2>

								<p>
									<B>1. 1852-1967. </B>Four combinations of nutrients were tested
									in a factorial design (strips 1-4; see plan): 0 v P v KMgNa v
									PKMgNa. This series was crossed by four series testing either
									no N or three forms of N usually applied at 48kgNha<sup>-1</sup>
									(Series O, no N; Series A, ammonium sulphate; Series AA, sodium
									nitrate; Series C, rape cake, later castor meal). Four
									additional plots in the south side test: unmanured (plot 61);
									ashes, 1852-1932 (plot 62); residues of FYM applied 1852-1871
									(plot 71); FYM since 1852 (plot 72).
								</p>
								<p>The sodium nitrate series was divided in 1862 for a test of
									with and without sodium silicate, to see what effect this had
									on straw strength, which was used in large quantities in the
									local hat factories at that time.</p>

								<p>
									<a href="<?=$metadata?>HFplan1852-1967.pdf" target="_blank">Hoosfield
										plan 1852-1967 (pdf).</A>
								</p>

								<p>The variety Plumage Archer was grown from 1917-1966. In 1927
									and 1928-32 alternate strips of Plumage Archer and Spratt
									Archer were grown for comparison. The Spratt Archer data is
									shown in dataset HOOSYIELD as SA.</p>

								<p>
									In 1964-1966, the plots were split in half, to compare the old
									variety, Plumage Archer, with the modern variety, Maris Badger
									(shown as plot name MB in dataset HOOSYIELD2). The amount of N
									applied to Maris Badger was increased to 96kgNha<sup>-1</sup>.
									Plumage Archer continued to receive 48kgNha<sup>-1</sup> and
									the other fertilizer and manure treatments were unchanged.
								</p>
								<p>There is no data for 1967, as the whole experiment was
									fallowed to control weeds.</p>
								<p>
									<B>2. 1968-1978. </B>In 1968, short-strawed cultivars were
									introduced and a three course rotation with potatoes and beans
									was started on part of the experiment (series AA and C). See <a
										href="<?=$metadata?>HFcropping sequence & N rates, rotation 
1968-78.pdf"
										target="_blank">Hoosfield rotation 1968-1978 (pdf)</A> for
									details of the cropping sequence and N fertilizer applied to
									the rotation each year.
								</p>

								<p>
									Also in 1968, most of the existing plots were divided into four
									main plots, testing four rates of fertilizer N, replacing the
									test of different forms of N. See <a
										href="<?=$metadata?>HFplan1968-1978.pdf" target="_blank">Hoosfield
										plan 1968-1978 (pdf).</A> The rates are N0, N1, N2 and N3,
									equivalent to 0, 48, 96 and 144 kg N ha <sup>-1</sup>. The
									rates were fixed from 1968-1973, there was then a transition
									period, 1974-1980, when the N rates changed after three and
									four years, then since 1981 the rates of N have changed every
									year in the order N3-N2-N1-N0. See <a
										href="<?=$metadata?>Hoosfield N rate sequence.xlsx"
										target="_blank">Hoosfield N rate sequence 1968-.xlsx </A> for
									details of the sequence of N fertilizer applied since 1968.
								</p>


								<p>
									<b>3. 1979-2000. </B>The three course rotation was discontinued
									in 1979 as the effects of a two year break were small. The
									testing of sodium silicates (Series AA) was modified in 1980 to
									test: 0 v silicate 1862-1979 v silicate since 1980 v silicate
									since 1862. See <a href="<?=$metadata?>HFplan1979-2000.pdf"
										target="_blank"> Hoosfield plan 1979-2000 (pdf).</A>
								</p>
								<p>
									<B>4. 2001 onwards.</B> Two new plots testing P2KMg (plot 63)
									and FYM (plot 73) were introduced in 2001 and Strip 5 tested
									various other combinations of N, P, K and Mg.
								</p>
								<p>In 2003, major changes were made. On the 'Main Plots', in
									addition to the four-level N test, P and Mg are withheld on
									some plots (and on parts of Series AA) until levels of plant
									available P and Mg decrease to more appropriate agronomic
									levels. This is reviewed every year.</p>

								<p>
									Response to plant-available P is tested in Series C and Strip
									5; basal N is applied and some plots receive K fertiliser to
									ensure K is not limiting yield. On Series AA the four-level N
									test was stopped in 2003 and basal N applied instead to
									simplify the silicate test. Sodium silicate, both as fresh
									application and residue, continued to give substantial yield
									increases in the period 2002-2005 on plots lacking P or K. <a
										href="<?=$metadata?>hoosfieldtoday.pdf" target="_blank">Hoosfield
										plan 2001 onwards (pdf)</A>
								</p>

								<p>
