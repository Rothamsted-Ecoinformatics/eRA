								<h2>Soil details</h2>
								<UL>
									<li>FAO Classification: Chromic Luvisol (or Alisol)</li>
									<li>U.S. Soil Taxonomy: Aquic (or Typic) Paleudalf</li>
									<li>Soil Survey of England & Wales Group: Stagnogleyic
										paleo-argillic brown earth (Avery, 1980)</li>
									<li>Soil Survey of England & Wales Series: Predominately
										Batcombe Series (Avery and Catt, 1995 � see pop-out soil map
										at top right of this page).
								
								</UL>
								<p>
									<b>Soil texture class: </b> Flinty silty clay loam topsoil over
									clay-with�flints (Avery and Catt, 1995). The soils contain a
									large number of flints and are slightly calcareous. Below about
									2m depth the soil becomes chalk. The experiment is
									under-drained and the site is free draining.
								</p>

								<p>
									<b>Soil texture,</b> 0-23cm (from Blake <i>et al</i>, 2003,
									plots 42 and 72)
								</p>
								<ul>
									<li>Sand: 28 %</li>
									<li>Silt: 52 %</li>
									<li>Clay (<2 &micro;m): 20 %</li>
								</UL>
								<p>From Avery and Catt (1995): Typical Batcombe series topsoil
									contains 18-27% clay, with clay content increasing with depth.</p>

								<p>
									<b>Soil pH:</b> Since the 1950s, chalk has been applied when
									and where necessary to maintain a soil pH of 7.0 � 7.5. Like
									several other old arable fields at Rothamsted, Hoosfield was
									given large dressings of chalk in the early part of the 19th
									century, when the practice was to dig out the underlying chalk
									and spread it on the arable land. Detailed surveys of soil pH
									and CaCO<sub>3</sub> around 1954 suggested that these early
									large dressings were not applied uniformly, and the amount
									applied lessened with increasing distance from one side or
									corner of the field. This meant that parts of the field became
									acidic earlier than others. (Warren and Johnston, 1967;
									Jenkinson and Johnston, 1977).
								</p>
								<p>
									<b>Soil movement:</b> Warren and Johnston (1967) and also
									Poulton (1996) discuss the early movement of soil across plot
									boundaries due to cultivation both along and across the plots.
									As a result of soil transects carried out in the 1950s,
									cultivations were restricted, and wide discard areas between
									plots introduced in the 1960s.
								</p>

