<h1>Hoosfield Spring Barley Experiment</h1>
<h2>
  Soil organic carbon content</h2>
<p>  The  Figure shows changes in soil organic carbon (SOC) content in selected  treatments of the Hoosfield experiment, where spring barley has been grown each  
year since 1852. SOC has remained almost constant in the unfertilized plot  since the 1880s, at the equilibrium level for this farming system on this soil  type. 
Annual applications of 35 t ha-1 FYM increased SOC rapidly at  first, then more slowly. This treatment now contains around three times as much  
SOC as the unfertilized plot. The third treatment, given FYM for just 20 years  between 1852 and 1871, still contains more SOC than the unfertilized plot. </p>
