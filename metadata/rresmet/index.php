<p>Hoos Barley has tested the effects of different fertilisers (N, P, K and Mg) and farmyard manure on the 
yields of spring barley since 1852. The experiment offers interesting contrasts to Broadbalk; for example, 
because it is spring-sown it has only been necessary to fallow it four times to control weeds and it showed 
that P is a more important nutrient for the spring-sown crop than for winter wheat. </p>

<p>As with Broadbalk, modifications were made to the experiment in 1968. Modern barley cultivars were introduced 
and a 3-course rotation was started on part of the experiment. This was discontinued in 1979 because the effects 
of a two-year break were small. One further change in 1968 was the introduction of different rates of fertiliser N; 
these rates were superimposed on treatments already receiving farmyard manure and different combinations of PKMg 
fertiliser. Unlike Broadbalk where the same amount of N is applied year-after-year to the same plot, on the Hoos 
Barley experiment the different rates of N are rotated. In 1862, a test of sodium silicate was introduced, originally 
to see what effect this had on the strength of the straw, which was used in large quantities in the local hat factories 
at that time. Although the effects on straw strength were contrary to what was expected i.e. more silicon made the straw 
more brittle, it was also found that the silicate increases yields on plots not given P, apparently because it increases 
the availability of the small amounts of P in these plots.   </p>

