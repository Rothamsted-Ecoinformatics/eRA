<H2>Description </H2>
<p>The accumulation of organic carbon in soil and tree biomass has been measured on two contrasting sites at Rothamsted that were fenced off in the 1880s and left to revert naturally to woodland.  Geescroft, on acidic soil  and Broadbalk [link to GC OA page], on calcareous soil. Previously, both sites had been in arable cultivation for centuries. </p>
<p>The <b>Geescroft</b> site was previously a long-term arable site, with natural regeneration of deciduous woodland following abandonment of arable cropping. It was originally part of an experimental field that grew beans (<em>Vicia faba</em>) from 1847-1878, with frequent breaks due to crop failure. This was followed by four years of bare fallowing 1879-1882. Clover was then grown 1883-1885, and last cultivated in spring 1883. In January 1886 the Wilderness area was fenced off and left uncultivated. Reversion is presumed to have started in spring 1883, after the last cultivation. The Wilderness covers part of the previous experiment on beans which included phosphorus treatments. Where possibl,e data has been given for soils taken from areas which had received no P fertilizer, although Jenkinson (1971) found no effect of P on the build up of organic C or N. </p>
<p> The acidic Geescroft Wilderness is now a deciduous wood dominated by oak (<i>Quercus robur</i>). In contrast, Broadbalk Wilderness [link] is now dominated by ash (<i>Fraxinus excelsior</i>), with sycamore (<i>Acer pseudoplatanus</i>) and hawthorn (<i>Craetagus monogyna</i>).</p>
<p>The figure  shows the accumulation of organic carbon in the soil, and tree root, litter and above-ground biomass in the Geescroft Wilderness, 1883-1999. The soil has been sampled to an equivalent depth of 69cm (see Poulton <i>et al</i>, 2003 for further details). Because the soil has become acidic, it is now covered by a permanent litter layer, in contrast to Broadbalk Wilderness, where each year's litter fall decomposes before the next arrives.  This site has gained 2.00 t C /ha/year over the 118 year period (0.38 t in litter and soil, plus an estimated 1.62 t in trees and their roots). Data on the accumulation of nitrogen is also available from the <a href="mailto:era@rothamsted.ac.uk" target="_blank">e-RA curators.</a></p>
<p><strong>SOIL</strong></p>
<p>Silty clay loam surface overlying clay-with-flints, over chalk at a depth of several metres.<br />
Topsoil (0-23cm) contains 20-25% clay, rising to 50-60% at depths of 1 metre.<br />
Stagnogleyic palaeo-argillic brown earth (Soil Survey of England and Wales classification).<br />
Aquic Paleudalf (USDA classification). <br />
Chromic Luvisol (FAO classification). <br />
Soil pH is near neutral (7.7 in 1999), as Broadbalk received large applications of chalk in the late 18th or early 19th centuries. <br />
Soil comparisons are made on an equivalent depth basis, to allow for changes in soil bulk density, see Poulton et al, 2003 for further details.<br />
The soil is now acid - the pH fell from 7.1 in 1883 to 4.4 in 1999. Geescroft never contained free CaCO3, although it occasionally received small amounts of chalk before the 1880s. </p>
<p><strong>SITE</strong></p>
<p>Rothamsted Research, Harpenden,  South East England<br/>
Longitude: 0 degrees 21 minutes West<br/>Latitude: 51 degrees 49 minutes North<br/>Altitude: 128 metres above sea level <br/>
Area: 1.3 ha</p>
<p><strong>TREATMENTS</strong></p>
<p>No fertilizer or manure has been applied since 1883. <br/>
Received small applications of chalk 1840s-1880s<br/>Continuous winter wheat 1843-1881, since 1882 uncultivated and unharvested<br/>
Regenerating natural woodland since 1883 (unplanted) - previously  long-term arable (field beans, Vicia faba, 1847-1878).</p>
