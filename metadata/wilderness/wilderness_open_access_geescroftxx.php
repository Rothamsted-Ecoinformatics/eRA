<H1>Broadbalk and Geescroft Wilderness Open Access Data</H1>
<p>Selected data from our long term experiments is being made freely available to the scientific community, as prepared summaries of commonly requested data. This recognises the national and international importance of the data. The aim is that greater use of the data will lead to further understanding and wider benefits. Rothamsted  relies on the integrity of the user to ensure that Rothamsted Research receives suitable academic acknowledgment as being the originators of these data, and offer assistance to users to help ensure that where these data are being applied they are represented and interpreted in a rigorous way. Please contact the <a href="mailto:era@rothamsted.ac.uk" target="_blank">e-RA curators</a> for help with questions about the data or its interpretation.</p>


<H2>Accumulation of organic carbon </H2>
<p>The accumulation of organic carbon in soil and tree biomass has been measured on two contrasting sites at Rothamsted that were fenced off in the 1880s and left to revert naturally to woodland. Previously, both sites had been in arable cultivation for centuries. </p>
<p>The <b>Broadbalk</b> site was part of the Broadbalk wheat experiment, and had grown unmanured winter wheat since 1843. The last wheat crop was sown in autumn 1881, but not harvested. The site was then fenced off and allowed to naturally revert to woodland. Large amounts of chalk had been applied to the soil in the late 18th - early 19th centuries, and the surface soil pH is still neutral (pH 7.7 in 1999). The site is small (0.2ha).</p> 

<p>The larger <b>Geescroft</B> site (1.3ha) grew beans for many years, and has been left uncultivated since 1883. In contrast to the Broadbalk site, it recieved very little chalk, and the soil is now acidic: soil pH fell from 7.1 in 1883 to 4.4 in 1999. </p>
<p>The calcareous site (Broadbalk WIlderness) is now dominated by ash (<i>Fraxinus excelsior</i>), with sycamore (<i>Acer pseudoplatanus</i>) and hawthorn (<i>Craetagus monogyna</i>); the acidic site (Geescroft Wilderness) is now a deciduous wood dominated by oak (<i>Quercus robur</i>). </p> 
<p>The figure below shows the accumulation of organic carbon in the soil and in tree root and above-ground biomass in the Broadbalk Wilderness, 1881-1999. The soil has been sampled to an equivalent depth of 69cm (see Poulton <i>et al</i>, 2003 for further details). The site has gained 3.39 t C/ha/year over the 120 year period (0.54 t in the soil plus an estimated 2.85 t in trees and roots). Unlike the Geescroft site, the amount of C in the litter layer is negligible, as the litter decomposes each year. Data on the accumulation of nitrogen is also available from the <a href="mailto:era@rothamsted.ac.uk" target="_blank">e-RA curators.</a> </p>

