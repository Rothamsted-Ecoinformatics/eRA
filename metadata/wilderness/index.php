<H2>Background</H2>

<p>Lawes set up the Broadbalk and Geescroft Wilderness experiments in the 1880s, as he was interested to see what plants would become established after abondoning arable cropping, and what changes might occur in the soil (Lawes, 1895). </p>

<p><b>Broadbalk Wilderness</b> was originally part of the Broadbalk wheat experiment, and had grown unmanured winter wheat since 1843. The last wheat crop was sown in autumn 1881, but not harvested. The site was then fenced off and allowed to naturally revert to woodland. In around 1900 it was divided into two halves, one remained as regenerating woodland, in the other half all woody species were removed ('stubbed') each year, to allow open ground vegetation to develop. In 1957 the stubbed section was divided into two, one half remains 'stubbed', the other half was mown for three years, grazed by sheep each year from 1960-2000, and has been mown each year from 2001 (herbage not removed). Data is not shown for the stubbed and mown areas, but is available from the <a href="mailto:era@rothamsted.ac.uk" target="_blank">e-RA curators.</a>  The surface soil is calcareous (pH 7.7 in 1999). </p> 

<p>See <a href="<?php echo $metadata ; ?>broadbalkplantoday.pdf" target="_blank">Broadbalk plan since 1968 (pdf) </A> to see a plan of the Broadbalk Wilderness, in relation to the rest of the Broadbalk wheat experiment.</p>

<p><b>Geescroft Wilderness</b> is a larger site about 1 km away from Broadbalk. It is also long-term arable land, and was originally part of an experimental field that grew field beans (<i>Vicia faba</i>) from 1847-1878, with frequent breaks due to crop failure. After bare fallowing for four years, clover was grown from 1883 to 1885, and the wilderness area was fenced off in January 1886. It has been uncultivated since spring 1883. The surface soil is now acidic (4.4 in 1999). </p>
 
