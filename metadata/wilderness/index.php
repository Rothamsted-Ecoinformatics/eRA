<H2>Vegetation</h2>
<p>The calcareous site (Broadbalk Wilderness) is now dominated by ash (<i>Fraxinus excelsior</i>), with sycamore (<i>Acer pseudoplatanus</i>) and hawthorn (<i>Craetagus monogyna</i>). The acidic site (Geescroft Wilderness) is now a deciduous wood dominated by oak (<i>Quercus robur</i>), with an understory of holly (<i>Ilex aquafolium</i>).  Because the soil is so acidic, there are few ground cover species, and there is a permanent litter layer, in contrast to the Broadbalk Wilderness, where each year's litter decomposes - see images to the right.</p> 

