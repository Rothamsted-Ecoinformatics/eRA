<H2>Site  and soil details</H2>

<p>
<b>Location</b>: Both Wilderness sites are at Rothamsted Research: 
<ul>
<LI>Location: Rothamsted Research, Harpenden, Herts, AL5 2JQ, UK</LI>
<LI>Latitude: 51 degrees 48 mins 36 secs North</LI>
<LI>Longitude: 0 degrees 22 mins 30 secs West </LI>
<LI>GB Grid Reference: TL121134</LI>
<LI>Gradient: A slope of 1 degree, West to East</LI>
<LI>Irrigation:  There is no irrigation</LI>
</ul>
</p>
<p><b>Size</b>: Broadbalk Wilderness is a small site, approx 0.2ha in total (woodland, stubbed and mown areas). Geescroft Wilderness is much larger, approx 1.3ha.</p>

<p><b>History</b>: Both the Broadbalk and Geescroft sites had been cultivated since at least 1623, and probably much earlier, 
see <a href="<?php echo $metadata ; ?>1623_map.jpg" target="_blank">Rothamsted map of 1623 </a>. </p>

<p>
<b>Soil details:</b> Batcombe soil series, silty clay loam surface overlying clay-with-flints, overlying chalk at a depth of several meters. The 0-23cm layer contains 20-25% clay, rising to 50-60% at depths of a meter or so (Poulton <i>et al</i>, 2003). 


<ul>
<li> FAO Classification:	Chromic Luvisol </li>
<li>U.S. Soil Taxonomy: 	Aquic  (or Typic) Paleudalf</li>
<li>Soil Survey of England & Wales Group: Paleo-argillic brown earth (Avery, 1980) </li>
<li>Soil Survey of England & Wales Series:  Predominately Batcombe Series (Avery & Catt, 1995 – see pop-out soil map at top right of this page).
</ul>
</p>


<p><b>Soil pH </b>:</p>
<ul>
<LI><b>Broadbalk Wilderness</b>: large amounts of chalk were applied in the late 18th - early 19th centuries, and the surface soil pH is still calcareous (pH 7.7 in 1999). </LI>
<Li><b>Geescroft Wilderness:</b> small amounts of chalk were applied in the 1840s-1880s, and the soil is now acidic (pH fell from 7.1 in 1883 to 4.4 in 1999).</LI>
</ul>


