<h1>Broadbalk and Geescroft Wilderness Experiments </h1>

<p>The Broadbalk and Geescroft Wildernesses are two sites at Rothamsted that were fenced off in the 1880s and left to revert naturally to woodland. The sites had previously been arable for centuries. Although not experiments in the usual sense, they are of great value, especially for looking at long-term changes in soil and woodland vegetation.  </p>

