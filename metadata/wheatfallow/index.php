<h1>Alternate Wheat and Fallow</h1>
<p>
The <B>Alternate Wheat and Fallow experiment</B> was started by Lawes and Gilbert in 1856, on a 0.4 ha site on part of Hoosfield that
had received no applications of fertilizers or manures since 1851. The experiment originally had two strips which alternated winter wheat 
and a bare fallow in successive years. Between 1932 and 1982, a modification was introduced to allow a yearly comparison of a one-year and 
a three-year fallow but the effects on the yield of the subsequent wheat crop were small and since 1983 the experiment has reverted to the original design.   </p>
<p>Since autumn 2015, the whole experiment (both plots) has grown winter wheat, with a small amount of fertilizer N applied in the spring. </p>

