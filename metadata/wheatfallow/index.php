<H2>Background</H2>
<p>No fertilizers or manures are applied but, like most of the other arable experiments, chalk is applied to maintain the 
soil at about pH 7. Pesticides are also applied when needed. All plots have been combine harvested since 1957. The plot area since 
1983 is 0.2 ha, from 1932-1982 it was 0.05 ha.  </p>
<p> The wheat cultivar grown has usually been the same as on Broadbalk, and the effects of fallowing on the yield of winter wheat can be roughly estimated 
by comparing yields from this experiment with continuous, unmanured wheat on Broadbalk (plot 03). In the first 10 years of the experiment the 
one-year fallow gave an extra 0.6 t ha<sup>-1</sup>, but over the next 60 years the difference was even smaller (0.14 t ha<sup>-1</sup>). Since its 
reversion to the original design in 1983, and with modern cultivars, average yields of the wheat after a one-year fallow have been 1.7 t 
ha<sup>-1</sup>. When expressed on the basis of the whole area (i.e. wheat plus fallow), the yield of 0.85 t ha<sup>-1</sup> is slightly less than
the 1.0 t ha<sup>-1</sup> for continuous wheat on Broadbalk.     </p>
<p>
In 1957 the original plots were divided into half longitudinally, the centre two strips continued in the wheat fallow cycle as 
before, and the two outer strips were used to study Wheat Bulb Fly.  From 1963 to 1966 the cropped plots were subdivided and two different 
varieties were grown (see <a href="<?=$metadata?>Wheat and fallow varieties.pdf">Varieties and cropping 1856-present</A> for details). 
From 1967 the experiment reverted to a single variety. </p>
<p>Since autumn 2015, the whole experiment (both plots) has been sown to winter wheat, the same variety as is grown on Broadbalk. A small amount of N fertilizer (50 kg N ha<sup>-1</sup>) is applied in spring (mid-April), but to maintain the low soil P and K status, no other fertilizers are applied. 
No yields or crop samples have been taken since harvest 2015. </p>

<p> It was in this experiment in 1935, that symptoms caused by the fungal pathogen <i>Gibellina cerealis</i> or 'white straw disease' were first recorded in the UK. </p>

