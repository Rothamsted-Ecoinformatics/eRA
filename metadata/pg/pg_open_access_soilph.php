<h2>Description</h2>
<p>The pH of the soil (at depth 0-23cm) on Park Grass was  approximately 5.5 (in water) when the experiment began. The effects of small  amounts of liming with chalk were tested in the 1880s. A regular test of liming  began in 1903 with chalk applied every 4 years to the southern half of most  plots. From 1965, the plots were further divided: two quarters of the  previously limed half receive amounts of chalk calculated to maintain at pH 7  (sub plot a) and 6 (sub plot b); one part of the formerly un-limed half  receives chalk, where necessary, to maintain pH 5 (sub plot c), and the other  quarter (sub plot d) remains un-limed.</p>
<p>Except where sodium nitrate (orange line N*) has been applied or where liming  with chalk has maintained the pH (dashed  lines), soil has acidified slowly through the impact  of acid deposition from the atmosphere and rapidly where ammonium fertiliser (blue line N) has been applied. Soil pH (0-23cm) is c 3.5 on the plots given most ammonium  sulphate and 5.0-5.3 on the un-limed, unfertilized plots. Sub plot c of most  plots is nearest to the original soil pH. Soil pH has slowly recovered by about  0.5 pH units on plot 9/1d (data not shown), more than 25 years after ammonium sulphate was last  applied.</p>
<p><strong>AGRONOMIC PRACTICES</strong></p>
<p>Fertiliser treatments since 1856;<br />
  <strong>No fertiliser</strong>: No fertilizer or manure<br />
  <strong>N*2PKNaMg:</strong> 96kgN as sodium nitrate plus P,K,Na and Mg<br />
<strong>N2PKNaMg:</strong> 96kgN as ammonium sulphate plus P, K, Na and Mg</p>
<p><strong>Chalk:</strong> Ground chalk (CaCO3) applied approximately every four years 1903 - 1964 then as required to maintain pH at around 6.</p>
