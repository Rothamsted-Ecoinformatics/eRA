<h1>Park Grass</h1>
<P>
The <b>Park Grass experiment</b> is an experiment on grassland laid down at
Rothamsted in 1856 in order to study the effects of applying different
fertilizers.
</P>
<P>
Since then, plots have been reduced in size and subdivided
for different liming treatments. On some plots there have also been
changes in the fertilizer treatment. However the treatment on some
subplots has remained unaltered.
</P>
