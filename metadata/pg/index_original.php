<h2>a Bit of History...</h2>

<p>The Park Grass experiment is the oldest on grassland in Great Britain and has been 
described as one of the most important in the world in the area of bio-diversity and 
bio-ecology. This experiment started in 1856, on a field that had been in pasture for 
at least a century. Various combinations of inorganic fertilisers (P, K, Mg, Na, nitrate-N,
 ammonium-N and Si) have been tested since the start; organic manures (farmyard manure and 
 fishmeal) have been tested since 1905.</p>
<p>
In 1903 most plots were halved and the effects of regular liming tested. 
This was modified in 1965 with the division of most plots into four sub-plots,
three of which are limed to maintain pHs of 5, 6 and 7. The fourth sub-plot receives 
no lime and the pH of these ranges from 3.5 to 5.7 depending on the fertiliser treatment. 
The plots are cut each year for hay, usually in June, and a second cut is taken in the
autumn.</p>

<p>Dramatically different swards have evolved as a result of the different pHs and nutrient statuses of the soils. There are 50 - 60 species on the unfertilised plots but only 2 or 3 species on some of the fertilised plots. Since 1990, nitrogen fertiliser has been withheld from half of all sub-plots formerly receiving 96 kg N ha-1 as either ammonium sulphate or sodium nitrate to study processes controlling soil acidification, heavy-metal mobilisation and botanical changes.</p>

<p>Studies have been made of the botanical composition of the plots both by visual surveys and by looking at the percentage contribution to the hay 
made by the various species. Empirical analyses of the crops and soils have been carried out and physical samples of the crops have been preserved.</p>

