<h2>Soil details</h2>
<UL>
<li> FAO Classification:	Chromic Luvisol  (or Alisol)</li>
<li>U.S. Soil Taxonomy: 	Aquic  (or Typic) Paleudalf</li>
<li>Soil Survey of England & Wales Group: Gleyic argillic brown earths (Hook) and Stagnogleyic paleo-argillic brown earth (batcombe) (Avery, 1980) </li>
<li>Soil Survey of England & Wales Series:  Predominately Hook Series, some also typical Batcombe (Avery & Catt, 1995 – see pop-out soil map at top right of this page). 
</UL>
  <p>
<b>Soil texture class: </b> Silty clay loam over clay-with-flints overlying chalk. The site is normally well drained (Hook series) or moderately well drained (Batcombe series) (Avery & Catt, 1995). </p>

<p><b>Soil texture,</b> 0-23cm, 2002 from six sub-plots (Poulton, <i>pers comm</i>):</p>
<ul>

<li>Sand (2000 - 63 &micro;m) :	11.6 %</li>
<li>Silt (63 - 2 &micro;m) :	66.3 %</li> 
<li>Clay (<2 &micro;m) :	22.1 %</li> 

</UL>
<p>
<b>Soil structure: </b> The structure of the topsoils are clearly influenced by organic matter content and base status. The calcareous plots have very dark coloured surface horizons, characterized by strongly developed granular or fine subangluar blocky peds. In contrast, the acid plots are more weakly structured and in extreme cases, typified by the unlimed plots which have received regular applications of ammonium sulphate (eg plots 4/2d, 9/2d and 11/2d), organic matter has accumulated at the surface to form a discrete mat or mor layer, and the immediately underlying mineral soil is massive and structureless (Avery & Catt, 1995). </p>

<p><b>Soil pH: </b> In 1965 most plots were divided into four sub-plots. Three subplots receive different amounts of lime as required to maintain pH at 7, 6 and 5 (sub-plots a, b and c, respectively). Sub-plot d receives no lime and pH of these ranges from 3.5 to 5.7, depending on the fertilizer treatment. See <a href= " http://www.era.rothamsted.ac.uk/Park/pg_open_access#SEC2" target="_blank"> Park Grass Open Access soil pH</a> for selected plots, for more details. </p></p>

<p><b>Soil sampling: </b>
A few plots were sampled in 1870 but the first major soil sampling was in 1876, 20 years after the experiment started. Soils were taken with an open-ended iron box, 
30.5cm x 30.5cm x 22.9cm deep. Just three samples were taken from each plot, as this method was disruptive on grassland. Since 1932, samples have been taken by straight-sided 
semi-cylindrical auger, bulking a large number of cores for each plot or sub-plot. All samples except in 1932 have been taken to a depth of 23cm, and in some years, deeper 
samples were also taken (Poulton, 1996). </p>

<p><b>Soil bulk density: </b>
A standard value of 1.1 g cm<sup>3</sup> (2500 t ha<sup>-1</sup>) should be used as a general indication of soil bulk density. This is the mean of all plots, sampled in 2011, 0-23cm only. Note that if detailed plot calculations are required, eg nutrient content,
individual plot data should be used - please contact the <a href= "<?=$Era_Curator[Email]?>"> e-RA Curators </a>  for this. Also, in some plots bulk density varies greatly within the 23cm depth particularly on acidic sub-plots with a
'mat' of semi decomposed organic matter on the surface. </p>



