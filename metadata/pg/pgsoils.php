<H1>Park Grass Soils</H1>
<p>The Park Grass experiment was started by Lawes and Gilbert in 1856 to look at the effects of applying fertilizers and manures on the yield of hay from permanent grassland. The field had been in permanent pasture for at least 100 years before the experiment began. It is never ploughed or sown. 
Here, details are given of the site and soil.  </p>
