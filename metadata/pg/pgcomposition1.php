<H1>Park Grass Botanical Compostion </H1>
<p>Studies have also been made of the botanical composition of the plots both through visual surveys and by looking at the percentage
contribution to the hay made by the various species. 

The most recent comprehensive surveys of botanical composition, <b>PARKCOMPIC</b>, made just before the first cut, were done annually between 1991 and 2000. See
  <a href="<?=$metadata?>Park Grass 150th Anniversary leaflet.pdf">150th Anniversary Publication (pdf)</a> for soil pH and a summary of those species comprising at least 10% of herbage, and the total number of species identified on each plot (after Crawley et al, 2005). There are many interactions between fertilizer, manure treatment and pH. Without exception, all the original treatments imposed in 1856 have caused a decline in species number
compared to the original sward. In most cases, this is due to changes in soil fertility and annual nutrient inputs and perhaps also the way in which
the sward was managed. Numbers of species have decreased, even on the plots given no fertilizers or manures, due to acid deposition. Applying nitrogen as
sodium nitrate or ammonion sulphate reduces diversity further, and in the ammonium form also rapidly acidifies the soil, reducing the number of
species to one or two, <i>Holcus lanatus</i> (Yorkshire Fog) and <i>Anthoxanthum odoratum</i> (Sweet Vernal Grass).</p>
<p><b>Survey method: </b>From 1991 to 2000, six randomly located quadrats measuring 50cm x 25cm were located within each of the plots in early June, immediately before the first cut. The herbage was cut with scissors to ground level and plant material taken back to the laboratory where it was sorted into species. Samples were oven-dried at 80 °C for 24 hours, after which dry mass was determined for each species. Data in <b>PARKCOMPIC</b> is shown as gm/0.125 m<sup>2</sup> for each quadrat and as gm/0.75m<sup>2</sup> for the total (ie 6 quadrats). 

<p>Further information is given in the <A HREF="https://doi.org/10.23637/rothamsted-long-term-experiments-guide-2006">
Rothamsted Guide to the Classical Experiments 2006</a>  pages 28-31, in <a href="http://www.open.ac.uk/science/biosci/personalpages/j.silvertown/pdfs/Crawley_etal_2005.pdf" target="_blank">Crawley et. al. (2005)</a> and in the Key References listed below. 
<p><b>Earlier surveys</b> of the botanical composition of Park Grass were carried out from 1862 to 1976. 
The dataset <b>PARKCOMP</b> consists of complete botanical analyses, carried out in 1862, 1867, 1872 and 1877, and 
several more times between 1903 and 1948. No analyses were carried out between 1949 and 1972. Further full botanical 
analyses were carried out on limited plots between 1973 and 1976. Full details are given by Williams 1978. Samples of plant material were
taken from each plot, separated into the different species and weighed. </p>
<p>The dataset <b>PARKPARTCOMP</b> contains a much simpler partial analysis of the three main groups of plants - grasses, legumes and
other species. These were carried out between 1862 and 1976. Again samples were taken from each plot, separated and then weighed to 
determine the percentage composition. </p>

<p>Except during 1895-1902, either complete or partial analyses were done in all years between 1874 and 
1948 for plots 3 (unmanured), 7 (PKNaMg) and 9 (N2PKNaMg). Full details are given by Williams 1978.</p> 

<p>Williams (1978) analysed selected plots between 1973 and 1976, after the sub-division of the Limed (L) and Unlimed (U)
plots into a, b, c and d sub-plots. Results for 1975 and 1976 in <b>PARKPARTCOMP</b> still refer to L and U half-plots. The L half-plot 
is equivalent to the a and b sub-plots, the U half-plot is equivalent to the c and d subplots. </p>

<p>Between 1920 and 1990 visual surveys were made twice a year, before the hay was cut in June and again in the autumn (<b>PARKVISUAL</b>).</p>

