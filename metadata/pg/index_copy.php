<h2>Background</h2>

<p>The experiment was established on c.2.8 ha of parkland that had been in permanent pasture for at least 100 years. It has had three main phases:</p>

<p><b>1. 1856-1902.</b> The uniformity of the site was assessed in the five years prior to 1856. Treatments imposed in 1856 included controls (Nil - no fertiliser
 or manure), and various combinations of P, K, Mg, Na, with N applied as either sodium nitrate or ammonium salts. FYM (farm yard manure) was applied to
 two plots but was discontinued after eight years because, when applied annually to the surface in large amounts, it had adverse effects on the sward. 
 For 19 years the re-growth was grazed by sheep penned on individual plots but since 1875 a second harvest has been cut and removed immediately. (link 
to plan) </p>
<p><b>2. 1903-1964.</b>  In 1903 plots 1-13, (except 5/1, 5/2, 6 and 12) and 16 were halved and the effects of regular liming tested. FYM, applied every four
 years, was re-introduced on three plots (13, 19 and 20) in 1905. In 1920, three more plots (14, 15 and 17), were halved and plots 18, 19 and 20 were 
divided into 3 subplots to test lime. (link to plan)</p>
<p><b>3. 1965 onwards.</b>  In 1965 most plots were divided into four sub-plots, three of which are limed to maintain pHs of 5, 6 and 7. The fourth sub-plot
 receives no lime and the pH of these ranges from 3.5 to 5.7 depending on the fertiliser treatment. Dramatically different swards have evolved as a result
 of the different pH and nutrient status of the soils. There are 50 - 60 species on the unfertilised plots but only 2 or 3 species on some of the fertilised
 plots. From 1965 plots 5/1, 5/2 and 6 were used for microplot experiments. Plots 6a and 6b were re-included in the main experiment in 1972 but the other
 half of plot 6 (6c and 6d) and plots 5/1 and 5/2 have remained outside the main experiment. Since 1990, nitrogen fertiliser has been withheld from half of
 all sub-plots formerly receiving 96 kg N ha-1 (plots 9 and 14) as either ammonium sulphate or sodium nitrate to study processes controlling soil
 acidification, heavy-metal mobilisation and botanical changes. Since 1995, plot 13 has been split into 13/1 and 13/2 and FYM/Fishmeal withheld from
 plot 13/1. Since 1996 plot 2 has been split into 2/1 and 2/2 with plot 2/1 receiving K as potassium sulphate. (link to plan)</p>

<p>The plots were originally cut by scythe, then by horse-drawn and then tractor-drawn mowers. They were cut each year for hay, usually in June, 
and a second cut taken in the autumn since 1875.  Yields were originally estimated by weighing the produce from the whole plot, either as hay 
(1st harvest) or green crop (2nd harvest), and dry matter determined. Since 1960, yields of dry matter have been estimated from strips cut with 
a forage harvester. However, for the first cut the remainder of each plot is still mown and made into hay, continuing earlier management and ensuring
 return of seed. For the second cut, the whole of each plot is cut with a forage harvester. Consequently recorded yields of dry matter are now larger than
 previously as fewer losses occur. The botanical composition of the plots has been studied by visual surveys and by looking at the percentage contribution
 of the various species to the hay harvested. Chemical analyses of the crops and soils have been made and physical samples of the crops and soils preserved. 

</p>

