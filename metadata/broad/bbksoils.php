<h2>Years when soil chemical properties were measured </h2>
<p>Soil chemical properties have been measured at regular intervals on Broadbalk since 1865, in topsoils (0-23cm) and subsoils. 
See <a href="<?php echo $metadata ; ?>Measurements of Broadbalk soil properties 1843-1944.pdf" target="_blank">soil measurements 1843-1965 </A> and 
<a href="<?php echo $metadata ; ?>Measurements of Broadbalk soil properties 1966-2012.pdf" target="_blank">soil measurements 1966-2012 </A> for details of what data is available.  

Not all plots, sections or soil depths 
have been measured every year. The following soil chemical properties have been measured:</p>
<UL>
<li>%N : total soil Nitrogen % </li>
<li>%SOC : soil organic Carbon % </li>
<li>Olsen P : plant-available phoshorus, by extraction with a solution of 0.5<i>M</i> NaHCO<sub>3</sub>, buffered at pH 8.5</li> 
<li>pH : soil pH in water (1:2.5 soil : solution) </li>
<li>Exchangeable cations : exchangeable Ca, K, Mg and Na </li>
<li>% CaCO<sub>3</sub> : by manometry </li>
<li>soil weights : see above for more details </li>
</UL>

<p>For the data and more details, contact the <a href= "<?=$Era_Curator[Email]?>"> e-RA Curators. </a> </p>


