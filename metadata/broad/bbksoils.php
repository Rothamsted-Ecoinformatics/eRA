<H2>Site details</H2>
<p>
<UL>
<LI>Location: Rothamsted Research, Harpenden, Herts, AL5 2JQ, UK</LI>
<LI>Latitude: 51 degrees 48 mins 36 secs North</LI>
<LI>Longitude: 0 degrees 22 mins 30 secs West </LI>
<LI>GB Grid Reference: TL123 136</LI>
<LI>Gradient: A slope of 1 degree, West to East</LI>
<LI>Irrigation:  There is no irrigation</LI>
<li>Plot sizes: The current plot lengths vary depending on the Section and are between 15.24m (Section 0) and 28.04m (Section 1). The plots are now 6m wide (except plots 2.1 and 2.2 which are 4m wide) with 48 rows at 12.5 cm spacing. Currently harvested area is plot length x 2.1m width. The harvested area is shown in most of the datasets. </li> 
</UL>
<A NAME="SEC2"></a>
<h2>Soil details</h2>
<UL>
<li> FAO Classification:	Chromic Luvisol  (or Alisol)</li>
<li>U.S. Soil Taxonomy: 	Aquic  (or Typic) Paleudalf</li>
<li>Soil Survey of England & Wales Group: Stagnogleyic paleo-argillic brown earth (Avery, 1980) </li>
<li>Soil Survey of England & Wales Series:  Predominately Batcombe Series (Avery & Catt, 1995 – see pop-out soil map at top right of this page).
</UL>
  <p>
<b>Soil texture class: </b> Clay loam to silty clay loam over clay-with flints. The soils contain a large number of flints and are slightly calcareous. Below about 2m depth the soil becomes chalk. The experiment is under-drained and the site is free draining. </p>

<p><b>Soil texture,</b> 0-23cm (from Gregory <i>et al</i>, 2010)</p>
<ul>
<li>Sand (2000 - 63 &micro;m) :	25 %</li>
<li>Silt (63 - 2 &micro;m) :	50 %</li>
<li>Clay (<2 &micro;m) :	25 %</li> 
<li>Particle density g cm <SUP><FONT SIZE = -2>-3</FONT></SUP> :	2.56 %</li>
</UL>
<p>
There is considerable variation in soil texture across the site, with clay contents ranging from 19 – 39% (Watts <i>et al</i>, 2006). The mean clay content for Sections 0 (straw incorporated since 1986) and 1 (straw removed, both continuous wheat) is 28.3% (Watts <i>et al</i>, 2006). Clay content increases with soil depth in the Batcombe Series (from Jenkinson <i>et al</i>, 2008):
<UL>
<li>23-46 cm :	30 % clay</li>
<li>46-69 cm :	50 % clay</li>
<li>69-92 cm :	49 % clay</li> 
</UL>
</p>
<p><b>Soil pH:</b> The plough layer (0-23 m) is limed when necessary to maintain a minimum soil pH of 7.0 – 7.5.</p>

<p>For information on soil organic carbon, see  <a href="Broadbalk/bbk_open_access#SEC2" target="_blank"> SOC Open Access dataset.</a></p>

<p>For information on soil concentration of Olsen P (plant-available P), see  <a href="Broadbalk/bbk_open_access#SEC3" target="_blank"> Olsen P Open Access dataset.</a></p>

 
<p><b>Soil Moisture Characteristics:</b>With nearly 200 plots in the experiment, there isn’t water retention information for all treatments. Water retention characteristics for contrasting plots can be found in Salter & Williams (1969). For more recent information, see Gregory <i>et al</i>, 2010, where retention characteristics have been fitted to the van Genuchten model. </p>

