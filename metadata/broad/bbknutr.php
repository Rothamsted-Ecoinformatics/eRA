<h2><strong>Methodology</strong> </h2>

<p>For analytical techniques used prior to 1968 see Johnston (1969) in Key References below.</p>
<h3><strong>Nitrogen (N)</strong> </h3>

<p><b>1968-1995:</b> nitrogen content was determined by Kjeldahl digest, the digest was then analysed colorimetrically using a Technicon segmented flow analyser. If nitrate-N was expected to be high in the sample, the salicylic acid modification was used (Bremner, 1965).</p> 

<p><b>1996 onwards:</b> nitrogen content is determined by combustion analyser, based on the Dumas method. Originally a Heraeus Combustion analyser, currently a LECO combustion system is used. </p>

<h3><strong>Phosphorus (P), Potassium (K), Calcium (Ca), Magnesium (Mg), Sodium (Na) and Sulphur (S)</strong> </h3>

<p><b>1968-late 1980s:</b> dry ashing techniques, as described by Piper (1942) were used to produce an acid extract. The extracts were analysed for P colorimetrically on a Technicon Auto Analyser, or later equivalents (Alpkem continuous flow system / Skalar SanPlus segmented flow system), using the modified Murphy & Riley (1962) molybdenum blue method developed from Fogg & Wilkinson (1958). K, Ca, Mg and Na were determined using automated atomic absorption methods. The SP90 atomic absorption flame photometer was used to analyse Ca, Mg and Na; K was analysed by the EEL flame photometer until 1973, K was then analysed with Ca, Mg and Na by the SP90. S was not routinely measured until 1996.</p>
<p><b>1980s onwards:</b> since the late 1980s the open tube nitric-perchloric digestion (Zarcinas, et al, 1987) has been used to produce an acid extract. P, K, Ca, Mg and Na have been determined by ICP-OES (Inductively-coupled plasma – optical emission Spectrometer) since 1982, and S since 1996.</p>   
<p><b>Blank (control) values:</b> Data for sodium is presented after the analysis blanks (controls) have been subtracted from the raw data. Sodium blank values tend to be relatively large, and can be greater than the raw data. If subtracting the blank resulted in a negative value, this is shown as zero. Thus the sodium data should be treated with some caution. Blank (control) readings for the other nutrients are very small, relative to the treatment values, and have not been subtracted. </p>
