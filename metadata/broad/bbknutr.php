<H1>Broadbalk Crop Nutrient Content</H1>
<p>The Broadbalk experiment was started in 1843 to investigate the relative importance of the plant nutrients nitrogen, phosphorus, potassium, magnesium and sodium (N, P, K, Mg and Na) on grain yield of  winter wheat. From the very start of the experiment, samples of wheat grain and straw were kept from each plot, for chemical analysis, though some early samples were lost because the containers were damaged. </p>

