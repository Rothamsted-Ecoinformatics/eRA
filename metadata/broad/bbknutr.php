<h2><strong>Early analysis</strong> </h2>
<p>Lawes and Gilbert carried out analyses of wheat grain and straw for N, P, K, Ca, Mg and Na content on proportionally bulked samples from 10 plots, representing four ten-year periods (1852-1861, 1862-1871, 1872-1881 and 1882-1891). The results  for crops grown in 1852-1871 were published in detail (Lawes & Gilbert, 1884). However, only the results for P, K, and Na content for four of the 10 plots for the second period (1872-1891) were published (Gilbert, 1895). Johnston (1969) summarises these early analyses.</p>

<p>Johnston (1969) also measured N, P, K, Ca, Mg and Na in grain and straw of the last two crops of Squarehead’s Master grown in 1966 and 1967, on samples taken from all plots in the continuous wheat section, last fallowed in 1951, and from all sections carrying the first wheat after fallow. These were compared with samples from 1852-91 (mean of 1966-67 only, not individual values).</p>

<A NAME="SEC2"></a>
<h2><strong>1968 to present</strong> </h2>
<p>In 1968 Broadbalk was divided into 10 sections. It was decided that it was no longer practical to keep samples for chemical analysis from all plots (10 sections x 19 plots). Each year only wheat grain and straw were kept from each continuous wheat plot on Section 1 (19 plots) and from plots where wheat followed potatoes and beans (Sections 2, 4 and 7, 1968-1978). Potato tubers and bean grain and straw samples were also kept. 
Dyke et al (1983) summarises plant nutrients in crops grown between 1968-1978. All samples from 1970-75 were analysed for %N, P, K, Ca, Mg and Na. Samples from 1976-78 were analysed for %N only.  </p>
<p>Thorne et al (1988) measured crop dry matter yield and %N, %P and %K from anthesis to maturity, from 1969-1984 on selected plots on Broadbalk – plots 07, 08, 15, 16, 17, 18, 21 and 22. Data was from first and second wheats in rotation with potatoes and beans. They also measured many other plant properties, including LAI, grain dry weight, number of ears, number of grains per ear, and total above-ground dry weight. Harvest index, date of anthesis and date of senescence were calculated. </p>
<p>From 1979-1985 %N was measured in grain and straw from the continuous wheat (Section 1) and the first wheat crop of the rotation, and in the potato tubers. From 1986-1995 %P, K, Ca, Mg and Na were also measured. </p>
<p>From 1996 %S was also measured (except in 2002). Nutrients were also measured in more continuous wheat sections (Sections 0 and 9 every year, and Section 8 in some years), and more of the rotational wheat sections. </p>

