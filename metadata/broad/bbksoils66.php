<H1>1966 Soil Sampling</H1>
<P>
<BODY BGCOLOR="#ffffff">

<BR>
In 1966 the surface soil of all plots was sampled with a semi-cylindrical auger, (0.75 in. diameter), to a depth of  9 inches (0-23cm).  Twenty cores were taken from individual plots within each section.  To avoid areas where soil may have been mixed by cultivation, only the central third of each plot was sampled.  Sections IA and VB are omitted for nitrogen and organic matter because they were not in the fallowing cycle.<BR>
<BR>
NB: Small diameter cores taken by semi-cylinders cannot be used to determine the weights of stones and fine soil per acre, but provided enough cores are taken, the sample represents the soil of the plot much better than the box samples for measuring the proportions of N, P and K.  For further information on difficulties of getting representative samples for both weights of soil/acre and nutrient content, as well as, any possible calculation errors please refer to Rothamsted Exp. Stn. Report 1968, Pt 2. Pages 94 and 96.<BR>
<BR>Samples were air-dried, sieved &lt 2mm and a sub-sample ground &lt 0.5 mm.  Determination of pH, NaHCO<SUB><FONT SIZE = -1>3</FONT></SUB>-soluble P, exchangeable cations were carried out on &lt 2mm sample; organic C and total N on &lt 0.5 mm sub-sample.
