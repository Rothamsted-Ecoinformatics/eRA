<H1>Broadbalk Weeds</H1>
<p>
Detailed visual surveys of weeds on Broadbalk were carried out every year between 1921 and 1979. Various different 
codes were used to indicate species abundance. The main ones are as follows:
</p>

<TABLE>
<TR><td class="border"> 0</TD><td class="border"> Occasional</TD></TR>
<TR><td class="border"> 0+</TD><td class="border"> Between 0 and T</TD></TR>
<TR><td class="border"> T</TD><td class="border"> Distributed</TD></TR>
<TR><td class="border"> T+</TD><td class="border"> Between T and P</TD></TR>
<TR><td class="border"> P</TD><td class="border"> Plentiful</TD></TR>
<TR><td class="border"> P+</TD><td class="border"> Between P and PP</TD></TR>
<TR><td class="border"> PP</TD><td class="border"> Very plentiful</TD></TR>
<TR><td class="border"> PP+</TD><td class="border"> Between PP and PPP</TD></TR>
<TR><td class="border"> PPP</TD><td class="border"> Extremely plentiful</TD></TR>
</TABLE>


<p> The following datasets are available: </p>
<p>BBKWEEDS for 1921-32 </p>
<p>BBKWEEDS_FAL for 1933-67 </p>
<p>BBKWEEDS_ROT for 1968-79</p>
<p>It is recommended that you extract both STATE and SPEC_REMARK since the presence of
a species is indicated by a remark such as 'patch' even though there is no code for the state. In view of the
large number of null values it is probably best to tick the checkbox to exclude them. However, remember that this doesn't 
mean that all the species extracted are actually present as the possible codes include '-' meaning none! By default a set 
of standard species names will be used, but, if you wish, you can choose to extract the names that were originally recorded. 
</p>
<p>You can choose to extract data from a particular plot
or species by choosing an item from the appropriate menu and can also choose a
range of values for the survey date. If a species name is chosen, taxonomical equivalents will also be
extracted. If the species belongs to a grouping, the grouping will
also be extracted, and if a grouping is chosen, its members will be
extracted. For example, if you choose to extract <I>Papaver
rhoeas</I>, <I>Papaver spp.</I> will also be extracted, and if you
choose <I>Papaver spp.</I> then <I>Papaver spp.</I>, <I>Papaver
rhoeas</I>, <I>Papaver dubium</I> and <I>Papaver argemone</I> will be
extracted. 
</p>



