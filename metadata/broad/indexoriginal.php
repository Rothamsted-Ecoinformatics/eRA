<H2>Introduction</H2>

<p>The first experimental crop of wheat was sown on Broadbalk in 1843 and
harvested in 1844. The aim of the experiment was to test the effect of
different organic manures and inorganic fertilizers on the yield of
winter wheat. For the first few years the treatments varied somewhat
but in 1852 a permanent scheme was established which has continued,
with some minor modifications, to today. </p>
<P>
The experiment has had three main phases: (<B>Click on a plan to display an enlarged version </B>).
 </p> <p>
1. 1844-1925  Wheat was grown continuously, apart from occasional fallowing
of parts of the field
</p> <p>

</p> <p>
2. 1926-1967  The field was divided into five strips, which were
fallowed in turn. In 1956 Section I was divided into two; IA was
assigned to continuous wheat with weedkillers as required but no fallows, 
while IB continued in the five year cycle. In 1963 Section V was divided into
two; VB was assigned to continuous wheat with weedkillers, while VA
continued in the five year cycle.
</p> <p>

</p> <p>
3. 1968- The field is now divided into ten sections, on some sections
wheat is grown continuously, while on others it is grown in rotation
with other crops.
</p> <p>
Grain and straw yields are recorded each year. Other data collected
include observations of weeds, pests and diseases and the results of
chemical analyses of crops and soils. Physical samples of crops and
soils have been preserved.
</p> 
