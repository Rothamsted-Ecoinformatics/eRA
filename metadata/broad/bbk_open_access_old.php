<H2>Broadbalk Yields</H2>
<P>

The Figure shows the mean long-term winter wheat yields from selected treatments on Broadbalk. The changes reflect the improved cultivars, 
cultivations and control of pests, diseases and weeds that have been introduced on Broadbalk, especially since the 1960s. 
  To control weeds, the  experiment was divided into five sections in the 1920s and one section bare  fallowed each year. The introduction 
of herbicides removed the need for  fallowing. Yields of continuous wheat given no fertilizer or manure have remained  at around 1 t ha<sup>-1</sup>. 
Since 1979 summer  fungicides have been used, which has allowed us to exploit the greater grain  yield potential of modern cultivars. In 1968 a 
rotation was  introduced on part of the experiment, so that it is now possible to compare the  yields of wheat grown continuously and as the 
first wheat after a two year  break. The highest yields are now from the first wheat crop in rotation, with the  best yields from fertilizer 
alone exceeding those from FYM alone, and the  combination of FYM + 96 kgN ha<sup>-1</sup> (144 kgN ha<sup>-1</sup> since 2005) often exceeding both.
