<h2>Description of the facility</h2>

<p>All the water leaving individual fields is channelled rhough a flume (15 in total) via preforated plastic pipe drains which are back-filled to the surfarce with stones. Each of the 15 flumes is fully instrumented to enable flow rates to be measured and water samples to be automatically collected and analysed. </p>
<p>Each farmlet carries its own herd of yearling cattle and flock of ewes and lambs. All animals will be weighed on and off the grazing to determine live-weight gain over the grazing period. during winter housing the animals will only be fed silage from the farmlet they were grazing.</p>
<p>Each of the 15 fields has a meteorological station that will monitor rainfall and soil water content and there is on e Metorological Office standard weather station situated at the centre of the platform providing a full suite of measurements.</p>
<p>Thee are three sets each of 12 LiCor automated chambers, one set for each Farmlet. These are operated at regular intervals to determine greenhouse gas fluxes.</p>
<p>Remote sensing is applied using proxies to the spatial variability of vegetation health, moisture and soil nutrinet content over the Farm Platform, extrcated from high resolution (5m pixel) satellite imagery.</p>
<p>The installed instrumentation at the Farm Platform enables a core set of data to be captured. Core data sets of water chemisrty is recorded at high resolution (12min intervals).</p>
