<h2>Description of the facility</h2>

<p>There are three farmlets each with five catchments - each catchment
	has one or more fields. Each farmlet is grazed by yearling cattle, ewes
	and lambs. All animals are weighed to determine live-weight gain over
	the grazing period. During winter housing the animals are only fed
	silage from the farmlet they were grazing.</p>
<p>All the water leaving individual catchments is channelled to a flume
	(15 in total) via perforated plastic pipe drains around the edges which
	are back-filled to the surface with stones. Each of the 15 flumes is
	fully instrumented to enable flow rates to be measured and water
	samples to be automatically collected and analysed.</p>
<p>Each of the 15 catchments has a meteorological station  that
	monitors rainfall, soil temperature and soil water content.</p>
<p>There are three sets each of 12 LI-COR automated chambers, one set
	for each Farmlet. These are operated at regular intervals to determine
	greenhouse gas (CO2 and N2O) fluxes.</p>
<p>Remote sensing is applied using proxies to the spatial variability of
	vegetation health and soil nutrient content over the Farm Platform,
	extracated from high resolution (20cm per pixel) imagery.</p>
<p>The installed instrumentation at the Farm  Platform enables a core set of data to be captured at high resolution. In  addition, data relating to the farm management is also recorded (See Parameters  Measured).</p>
