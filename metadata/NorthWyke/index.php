<h1>North Wyke Farm Platform and associated data in e-RA</h1>

<p>The Farm Platform is located in North Wyke,  Devon on the edge of Dartmoor and is a new BBSRC funded UK national capability  with three hydrologically-isolated farmlets each consisting of  five hydrologically-isolated units of land ('catchments'). The underlying  principle is to manage each of these 22ha farmlets differently: improvement  through use of mineral fertilisers; improvement through use of legumes;  improvement through innovation. The overall hypothesis being that we can  manipulate grassland systems to optimise production whilst minimising the  environmental impact. The connectivity between the timing and intensity of  different management operations and the transport of nutrients and potential  pollutants from the farm is being evaluated using the latest sensor technology  coupled with more traditional field study methods and detailed farm management  records allows us to better understand processes and underlying mechanisms that  can be used to model how agro-ecosystems will respond to changes in management.  As a National Capability, we will provide the set of core data from across the  Platform that will be available to all research users and collaborators.</p>
	
