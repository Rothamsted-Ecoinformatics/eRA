<H1>Exhaustion Land Experiment Soils</H1>
<p>The Exhaustion Land experiment was started in 1856 on a site on Hoosfield, which has probably been occupied since Roman times. The Rothamsted map of 1623 shows 
the site under arable cultivation. Unmanured winter wheat was grown 1852-1855 (Johnston & Poulton, 1977). Here, details are given of the site and the soil.</p>
