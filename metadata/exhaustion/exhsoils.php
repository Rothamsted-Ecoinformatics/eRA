<h2>Soil details</h2>
<UL>
<li> FAO Classification: Chromic Luvisol  (or Alisol)</li>
<li>U.S. Soil Taxonomy: Aquic (or Typic) Paleudalf / Typic Paleudalf (or Hapludalf)</li>
<li>Soil Survey of England & Wales Group: Stagnogleyic paleo-argillic brown earth / Typical paleo-argillic brown earth (Avery, 1980) </li>
<li>Soil Survey of England & Wales Series:  Batcombe-Carstens Series (Avery and Catt, 1995 – see pop-out soil map at top right of this page).
</UL>
  <p>
<b>Soil texture class: </b> Silty clay loam topsoil over clay-with–flints (Avery & Catt, 1995). The soil contains a large number of flints and some rounded pebbles from the 
material derived from the Reading Beds. The soil is naturally acid and free draining. Below about 2m depth the soil becomes chalk. </p>

<p><b>Soil texture,</b> 0-23cm (from Poulton <i>et al</i>, 2013)</p>
<ul>
<li>Sand:	28 %</li>
<li>Silt:	52 %</li>
<li>Clay (<2 &micro;m):	20 %</li> 
</UL>
<p>Typical Batcombe-Carsten series topsoil contains 18-27% clay, with more clay at depth (Avery & Catt 1995). </p>

<p><b>Soil pH:</b> Since the 1950s, chalk (CaCO<sub>3</sub>) has been applied as necessary to maintain a minimum soil pH of 7.0. Like several other old arable fields at Rothamsted, 
Hoosfield was given large dressings of chalk in the early 19th century, when the practice was to dig out the underlying chalk and spread it on the arable land. 
Chalk was applied at up to 250 t/ha, and this increased topsoil pH in water (23cm) to between 7.0 and 8.0 (from Poulton <i>et al</i>, 2013). 
</p>



