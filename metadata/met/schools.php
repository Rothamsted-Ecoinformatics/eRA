<H2>Background</H2>
<p> <b>History of weather measurements at Rothamsted:</b></p>
<p>Sir John Lawes, the founder of Rothamsted, realized that to understand crop growth, it was important to have records of the weather. Weather records also help us to understand the spread of crop diseases and insect behaviour. Rainfall and wind direction have been measured at Rothamsted every day since 1853, temperature since 1878 and sunshine since 1890, making this one of the longest sets of meteorological (weather) data available in the UK. As new equipment was invented to measure different meteorological variables, scientists added them to the Rothamsted weather station. Now over 25 different weather measurements are made every day. </p>
<p><B>How the data is measured:</B></p>
<p>Originally all the weather data were recorded by hand. Every morning at 9am a scientist would go out to the weather station, seven days a week, 365(6) days a year. Then in 2004 the Rothamsted weather station was fully automated, and the weather data is now measured by a range of sensors attached to an automatic data logger, linked directly to a computer.</p>
