<H2><strong>Description of the Rothamsted weather data variables (met data)</strong></H2>
<br clear="all" />

<img class="intext" src="<?=$metadata?>/RES rain_gauge.jpg" title="Rothamsted rain gauge" width="110" height="146" align="left" hspace="5" vspace="5" />
<p><b>Rainfall (RAIN):</b> This is total rainfall over 24 hours, measured in mm. It includes rain, snow, mist and fog (together called precipitation).  Originally the rain was collected in a copper funnel rain gauge, 
          and the water measured manually in a graduated cylinder. Since 2004, rain is measured using an electronic tipping bucket rain gauge, calibrated to tip at 0.2mm. This means that 
since 2004, the minimum amount of rain that can be recorded is 0.2mm. These rain gauges are surrounded by a 1.5m radius turf wall, 30cm deep, to reduce wind eddies blowing the rain out of the gauges.</p>
<br clear="all" />

<img class="intext" src="<?=$metadata?>/RES sun recorder.jpg" title="Rothamsted pyranometer" width="110" height="163"  align="left" hspace="5" vspace="5"  /></td>
      <p><b>Sunshine (SUNHOURS):</b> The total amount of time when the sun shines during each day. This is measured in hours,  for example, 10.5 hours means 10 hours and 30 minutes.   
          Hours of sunshine were originally measured by a Campbell-Stokes sunshine recorder. The sun's rays were focused onto a card (treated to prevent it from catching fire) 
          and the brown scorch mark on the card was then measured. The longer the scorch mark, the longer the hours of sun. The cards were of varying lengths applicable to 
      the time of year (winter, equinox, and summer). Since 2004 sunshine has been calculated using the Campbell-Stokes equation from solar radiation measured using a thermopile pyranometer.</p>
<br clear="all" />


<img class="intext" src="<?=$metadata?>/RES thermometers.jpg" title="Rothamsted thermometers" width="110" height="163" hspace="5" vspace="5" align="left" />
<p><b>Temperature (TMAX and TMIN):</b> The maximum and minimum air temperatures during the day, measured in &deg;C. TMIN should be less than or equal to TMAX. The daily temperature is the mean of TMAX and TMIN ((TMAX + TMIN)/2). Temperature used to 
      be measured with a maximum and minimum mercury thermometer. Since 2004, electronic temperature probes have been used, which are linked to the automatic 
      data logger. The thermometers are housed in a unit called a Stephenson Screen to ensure shade from sun - ventilation is provided by slits in the sides.</p>
<br clear="all" />

<img class="intext" src="<?=$metadata?>/RES wind_tower.jpg" title="Rothamsted wind tower" width="110" height="173" hspace="5" vspace="5" align="left"/>


<p><b>Wind (WDIR):</b> Wind direction, measured at 09.00 GMT each morning. This is shown as an angle, going clockwise from the North. 360 = North, 90 = East, 180 = South, 
  270 = West. The reading is in degrees. 0 degrees means that there is no wind. Wind direction was originally measured by a wind vane linked to a Munro roll chart 
recorder. From 2004 an electronic wind vane linked to the automatic data logger has been used. See video of wind direction recorder (above) to show this in action.<b> Wind run (WINDRUN):</b> The total amount of wind in a day, measured as a distance (km). Wind run is measured by a cup anemometer, which is blown around by the wind, 
and the distance it turns around is recorded, in km. This is on a pole 2m high. Since 2004, an electronic cup anemometer has been used, which is linked to the automatic data logger. See video of wind speed recorder (above) to show this in action.</p><br clear="all" />
