<H2>Long-term weather</H2>
<p>The long-term weather records at Rothamsted have been invaluable in aiding understanding of the effects of climate on crop growth, yields and diseases. They have revealed changes in temperature and rainfall over 170 years. The annual mean air temperature has fluctuated considerably and rose sharply between 1987-2007.  </p>
<p>
The figure shows the annual mean air temperature at Rothamsted every year from 1878 - 2013. Also shown is the mean over each five year period, 1878-1882, 1883-1887, etc. The red line shows the mean temperature, 1878-1987. The mean air temperature at Rothamsted is now 10 ºC, 1 ºC higher than the 1878-1987 average. The 10 warmest years on record occured in the last 17 years. Mean soil temperatures have also risen. </p>
