<p><b>Location of the weather station:</b> </p>
<p>The weather data stored in our electronic database (e-RA) is recorded by the Rothamsted weather station in Harpenden, Herts. Its specific location is Latitude 51.82 North &amp; Longitude 0.37 West. It is 128 metres above sea level. GB Grid Reference: TL121134. The site is fairly open and surrounded by sports fields and arable fields.</p>

<p><strong><b>The e-RA database:</b></strong> </p>
<p>e-RA is an on-line database developed by Rothamsted scientists to provide safe and secure storage of the data from Rothamsted's long-term experiments and meteorological (weather) data. The data are freely available  for use by scientists and universities. A subset of weather data have been made specifically available for schools. </p>
