<h1>Rothamsted Weather data available in 'Rothmet'  </h1>

<p>
Daily meteorological data recorded at  Rothamsted, Harpenden, Hertfordshire available from e-RA. Hourly data for Rothamsted  from 2004 is available from the <a href= "mailto:era@rothamsted.ac.uk"> e-RA Curators.</a> </p>
