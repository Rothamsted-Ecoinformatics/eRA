<H2><A NAME="SEC5" HREF="metdata_toc.html#SEC5">Sunshine</A></H2></p>
<P>
Sunshine readings, or more correctly hours of bright sunshine, were
first recorded in 1892 using a Campbell-Stokes sunshine recorder,
similar to the instrument in use today. The instrument works by
focussing the sun's rays and burning a mark on a card which is then
measured. The cards are specially treated to prevent them catching fire
and are of varying lengths applicable to the time of year; winter,
equinox (used twice a year) and summer. (Go to section <A HREF="metdata_2.html#SEC20">Sunshine hours, cloud cover and solar radiation</A> to
select this variable for extraction).</p>
<P>
<H2><A NAME="SEC6" HREF="metdata_toc.html#SEC6">Solar Radiation</A></H2></p>
<P>
Solar radiation has been recorded at Rothamsted since 1921 using various
instruments. Presently a Kipp and Zonen pyranometer is used, which is
the world meteorological standard; the signal is recorded on an
integrator and chart recorder. (Go to section <A HREF="metdata_2.html#SEC20">Sunshine hours, cloud cover and solar radiation</A> to select this
variable for extraction).</p>
<P>
<H2><A NAME="SEC7" HREF="metdata_toc.html#SEC7">Wind</A></H2></p>
<P>
Although wind estimations using the Beaufort scale have always been used
at Rothamsted, it was not until 1944 that an anemometer was installed.
This gives the run of wind per day, for many years measured in miles per
day, but now as kilometers per day at 2m above the ground. We also have
the Dines anemograph to continually measure wind speed and direction.
This was installed in 1978 and replaced an older pressure tube
anemograph dating from 1954. The wind vane is at 42 feet above ground
level. (Go to section <A HREF="metdata_2.html#SEC21">Wind data</A> to select variables for extraction).</p>
