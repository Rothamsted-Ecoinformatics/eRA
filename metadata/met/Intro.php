<H2><A NAME="SEC4" HREF="metdata_toc.html#SEC4">Temperature</A></H2></p>
<P>
Lawes and Sir Henry Gilbert began to look at the whole of plant growth.
Many other climatic conditions affect plant growth other than the rain
they had measured until now. Temperature and sunshine give the plant
energy to use the nutrients released by rain and in 1878 they started to
measure the air temperature with thermometers very similar to those we
still use today. Readings were taken of current dry and wet bulb
temperatures to calculate humidity etc, and maximum and minimum
temperature of the day and night. (Go to section <A HREF="metdata_2.html#SEC11">Air temperature data</A> to
select variables for extraction).</p>
<P>
Later, in the 1920s, it became possible to record soil temperature using
specially adapted thermometers. These were originally set at depths of
4, 8, 12, 24 and 48 inches under grass cover, and 4, 8 and 12 inches
under bare soil. Although we still use the same type of thermometers
today we have "gone metric" and record at depths of 10, 20, 30, 50 and
100cm. All temperatures are now in degrees centigrade, the change from
Fahrenheit taking place in 1970. (Go to section <A HREF="metdata_2.html#SEC12">Soil temperature data</A> to
select variables for extraction).</p>
