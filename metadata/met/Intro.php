<H2><A NAME="SEC8" HREF="metdata_toc.html#SEC8">Other observations</A></H2></p>
<P>
Many other observations are taken including measuring evaporation from
an open water tank, amount of cloud, state of the ground surface,
visibility, current weather and barometric pressure. A comprehensive
diary is also kept using the Beaufort letter shorthand.</p>


