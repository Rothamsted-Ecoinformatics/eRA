<H2><A NAME="SEC3" HREF="metdata_toc.html#SEC3">Measuring Percolation</A></H2></p>
<P>
Although Lawes had built the 1/1000th acre raingauge, he still had the
problem of where the excess water was going and what nutrients were
being lost (leached) by drainage. In 1870 the three percolation gauges
(or drain gauges as they are sometimes known) were brought into use.
They consist of undisturbed blocks of soil of 20 inches (51cm), 40
inches (102cm) and 60 inches (152cm) in depth and of area equal to the
raingauge at 1/1000th of an acre.</p>
<P>
The gauges were constructed by digging round and under the block of
soil, placing perforated steel plates underneath at the required depth
and bricking up the sides. The soil inside the gauges remained
undisturbed throughout the construction process. He now had the ability
to not only measure the rainfall, but also to find out how much of the
rain was draining through the soil and, by analysis, how much nutrient
was being lost. The drainage water was also originally measured by
weighing the carboys of collected water, but these too were converted to
calibrated cylinders. All three gauges remain very much as built and are
still in use for their original purpose. The soil has never been deep
cultivated, only the top is kept clear by hand weeding, and they have
never been cropped.  (Go to section <A HREF="metdata_2.html#SEC19">Drainage data</A> to select variables for
extraction).</p>
