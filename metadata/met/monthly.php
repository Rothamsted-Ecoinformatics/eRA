<p> <b>October 2017</b>: October was relatively warm, dry and cloudy for the time of year. </p>
<p>Rainfall was 31.2 mm which is 50.51 mm below average. We had 19 days with 0.2 mm or more of rain and 2 with over 5.0 mm. The greatest fall was 8.6 mm which fell on the 18th.  </p>
<p>Sunshine was below average with 87.4 hrs (average for October is 111.7) the most was 8.3 hours on the 27th. There were 3 days without sunshine.  </p>
<p>October temperatures were above average with the mean maximum and minimums being recorded as 15.4ºC (+1.37) and 9.1ºC (+1.99) respectively. The highest temperature was 21.1ºC on the 16th and the lowest was 1.9ºC on the 30th.  </p>
<p>There were 3 ground frosts.</p>
<hr />
<p> <b>Septmber 2017</b>: September was relatively cloudy, cool and wet for the time of year. </p>
<p>Rainfall was 86.9 mm, which is 29.29 mm above the average. We had 23 days with 0.2 mm or more of rain and 5 with over 5 mm. The most was 16.1 mm on the 8th.  </p>
<p>Sunshine was 116.9 hrs which is 26.44 hrs below average. There was only 1 day without sunshine and the most recorded was 8.4 hours on the 22nd.  </p>
<p>The mean maximum and minimum temperatures were below average with 17.3°C (-0.94) and 9.8°C (-0.17) respectively. The highest (daytime) temperature was 21.4ºC on the 4th and the lowest (night) temperature was 4.5ºC on the 22nd.  </p>
<p>There was 1 ground frost recorded.</p>
<hr />
<p> <b>August 2017</b>: August was relatively cloudy, cool and damp for the time of year. </p>
<p>Rainfall was above average with 66.6 mm (+2.89mm). We had 14 days with 0.2mm or more of rain, 6 of which had more than 5.0 mm; the most was 14.2 mm on the 8th.  </p>
<p>The mean maximum and minimum temperatures were below average with 20.2ºC (-1.39°C) and 11.8ºC (-0.03°C) respectively. The highest temperature was 26.1ºC on the 28th and the lowest was 6.5ºC on the 31st.  </p>
<p>Sunshine was 26.04 hours below average with 170.2 hours. The sun shone on all but 2 days and the sunniest day was the 13th with 11.6 hours.  </p>
<p>There were no frosts.</p>
<hr />
<p> <b>July 2017</b>: July was a wet and dull month with average temperatures equal to or above average.  <br />
  <br />
Rainfall was above average with 72.6 mm, which is 22.69 mm above the mean. We had 16 days with 0.2mm or more of rain with four days having more than 5 mm.  The greatest rainfall was 19.9 mm recorded on the 11th. <br />
<br />
Sunshine was below average with 187.4 hrs. 17.8 hours below the average. The most was 14.4 hours on the 5th and there was sunshine recorded all days.<br />
<br />
The average maximum temperature was equal to the 30 year mean at 21.8 &deg;C (+0.00) and the average minimum temperature was above average with 13.2 &deg;C (+1.31). The warmest day reached 29.8 &ordm;C on the 6th.  The lowest minimum temperature was 10.8 &ordm;C on the 4th.<br />
<br />
We had no air or ground frosts.</p>
<hr />
<p> <b>June 2017</b>: <span id="e">June was particularly warm, sunny and dry.  <br />
    <br />
Rainfall was 39.1 mm, which is 14.12 mm below the mean. We had 12 days with 0.2 mm or more of rain, of which only 2 days had more than 5.0 mm.  The wettest day was the 5th with 16.6 mm. <br />
<br />
Sunshine was above average with 229.1 hours (average for June is 198.2 hours); the most was 13.7 hours on the 19th and the sun shone on all but 1 day. <br />
<br />
The mean maximum and minimum temperatures were both above average with 21.5&ordm;C (+2.38&deg;C) and 12.0 &ordm;C (+2.25&deg;C) respectively. The hottest day reached 31.3&ordm;C on the 21st and the coolest night dropped to 7.26 &ordm;C on the 4th. The highest maximum was the 4th highest in our records (the highest was in June 1976 with 33.0&deg;C). The overall average temperature was the 2nd highest in our records (the warmest was June 1976 with an average of 17.2&deg;C). <br />
<br />
There was 1 ground frost but no air frosts.</span></p>
<hr />
<p><strong>May 2017:</strong> May had above average air temperatures and was relatively cloudy and wet for the time of year.<br />
  <br />
Rainfall was 70.5 mm, which is 15.82 mm above the mean. We had 16 days with 0.2 mm or more of rain and 4 days with more than 5.0 mm.  The most was 22.3 mm on the 17th of May.<br />
<br />
Sunshine was below average with 185.1 hrs. (-9.57 hrs.); the most was 14.8 hours on the 26th. There was only 1 day without sun.<br />
<br />
Both the average maximum and minimum temperatures for May were above average with 17.7&ordm;C (+1.65&deg;C) and 8.7&ordm;C (+1.79&deg;C) respectively. The highest (max) temperature was 24.6&ordm;C on the 26th; the lowest (min) temperature was -0.3&ordm;C on the night of the 10th. The average minimum temperature is notable as being the second highest since records began in 1878 (the highest since May 2008).<br />
<br />
We had 2 ground frosts and 1 air frosts.</p>
<hr />
<p>

	<b>April 2017</b>: <span id="e45">April continues a trend from the previous two months with above average temperatures and below average rainfall. Sunshine was above average for the time of year and light sleet/snow showers were reported towards the end of the month.<br />
	<br />
Rainfall was 10.9 mm which is 44.19 mm below average. We had 14 days with 0.2 mm or more of rain, and no days with more than 5mm.  The wettest day was the 30th with 4.7 mm. Sleet/snow showers were observed on the afternoons of 24th and 25th.<br />
<br />
Sunshine was above average with 197.4 hours (+36.17); the most was 12.4 hours on the 19th and there was only one day with no sunshine.<br />
<br />
The average maximum and min temperatures were both above average with 13.8  ºC (+1.20) and 4.2  ºC (+0.13) respectively. The warmest day was the 9th which reached 23.9 ºC, and the coldest night was on 
the 27th when it dropped to -0.8 ºC.<br />
<br />
We had 2 air frosts and 17 ground frosts.<br />
	</span>

  <!--      end             ------><!--      begining             ------></p>
<hr />
<p>

	<b>March 2017</b>: March was relatively dry and sunny with very high average temperatures for the time of year.<br />
	<br />
Rainfall was below average with 40.4 mm recorded, (-10.42 mm). We had 21 days with 0.2 mm or more of rain, and 2 days with more than 5 mm. The most recorded was 7.58 mm on the 22nd. <br />
<br />
Sunshine was above average with 139.9 hrs (average for March is 114.9); the most recorded was 11.6 hours on the 25th. There were 3 days without sunshine. <br />
<br />
The mean maximum and minimum temperatures were above average with 12.7 ºC (+2.76) and 5.0 ºC (+2.33) respectively. The highest maximum temperature was 20.3 ºC on the 30th and the lowest minimum was 1.1 ºC 
on the 6th.  The overall average air temperature for the month was the 2nd highest since our records began in 1878.<br />
<br />
We had 0 air frosts and 15 ground frosts. <br />

<!--      end             ------><!--      begining             ------></p>
<hr />
<p>

	<b>February 2017</b>: February was relatively cloudy for the time of year but was also warmer and drier than average. There were reports of some snow flurries in the area but none of this settled significantly. <br />
	<br />
Rainfall was below average with 38.7 mm (-11.40). We had 18 days with over 0.2 mm, and 1 day that exceeded 5.0 mm. The greatest rainfall was 9.2 mm on the 6th. Some of the month&rsquo;s precipitation fell as light snow and sleet flurries over the weekend of the 10th,11th and 12th as well as the 28th.<br />
<br />
Sunshine was below average with 62.8 hrs. (-17.48). We had 4 days without any sunshine with the most being 9.0 hrs on the 13th. <br />
<br />
The mean maximum and the mean minimum temperatures were above average with 8.5 ºC (+1.59) and 3.2 ºC (+2.29) respectively. 
The lowest minimum was -1.8 ºC on the 6th, and the highest maximum was 16.0 ºC on the 20th. <br />
<br />
We had 5 air frosts and 15 ground frosts.</p>


<!--      end             ------><!--      begining             ------>
<hr />
<p>

	<b>January 2017</b>: January was relatively cool with above average sunshine. Precipitation was close to the 30-year average and there was some light sleet and snow in the middle of the month.<br />
	<br />
Rainfall was above average with 70.2 mm (+0.21 mm). We had 23 days with 0.2 mm or more of rain and 5 had more than 5.0 mm. The most on one day was 11.5 mm on the 29th.  Some of the precipitation fell as snow or sleet on the evening of the 12th and also on the morning of the 13th. <br />
<br />
Sunshine was above average with 77.2 hours (+15.19); the most was 7.4 hours on the 22nd. We had 9 days with no sunshine.<br />
<br />
January&rsquo;s mean maximum and minimum temperatures were below average at 6.3 ºC (-0.38) and -0.2 ºC (-1.40) respectively. The highest day temperature was 10.0 ºC on the 10th. 
The lowest minimum was -6.0 ºC on the 22nd.<br />
<br />
We had 19 air frosts and 19 ground frosts.</p>
