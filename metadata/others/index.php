<A NAME="SEC11"></a>
<H2>Woburn long-term sludge experiments</H2>

<div align="center">
	<img src="<?=$metadata?>cs-427_ii_675.jpg"
		title="Woburn sewage sludge CS427" width="675" height="274"
		alt="Woburn sewage sludge CS427" />
</div>
<p>
	The <b>Woburn long-term sludge experiments</b> were started in 1994, at
	Butt Close field, to study the effects of heavy metals contained in
	sewage sludges on soil fertility and microbial activity. They are part
	of a network of nine sites throughout the UK.
</p>

<p>
	<b>Design: </b>Three experiments in Butt Close field: 1) sewage cake
	experiment (CS/427), 2) liquid sludge experiment (CS/428), and 3) metal
	salts experiment (CS/439). Details can be found in Gibbs et al (2006
	a,b) and Chaudri et al (2008).
<p>
<p>
	<b>Experiment CS/427:</b> Five types of sewage sludge cake were
	applied, (including place of origin):
<UL>
	<li>Digested low metal sludge (D; Banbury)</li>
	<li>Digested sludges contaminated with either Zn (DZn; Coleshill)</li>
	<li>Digested sludges contaminated with either Cd (DCd; Perry Oaks)</li>
	<li>Undigested low metal sludge (U; Carterton)</li>
	<li>Undigested sludge contaminated with Cu (UCu; Selkirk)</li>
</UL>
</p>
<p>Sludges were applied in two different ways, as short-term (ST)
	treatments applied in four large applications between 1994 and 1997 or
	as long-term (LT) treatments of annual applications between 1994 and
	2014 of approximately 1/25th of the amounts of the same sludges to
	realize the same total dose of metal application as in the ST
	treatments in 25 years.</p>
<p>Note that U and D refer to the original sludge treatment process.
	After 1997, these sludges were air-dried and stored for use during the
	long-term treatment period. A control receiving no sludge was included
	in the experiment. Treatments randomised in 3 blocks 6 x 8 m in size.</p>

<p>
	<b>Experiments CS /428 and /439: </b> see below, and Gibbs et al (2006
	b) and Chaudri et al (2008).
</p>

<p>
	<b>Strengths:</b> CS/427 enables comparison of different sewage sludge
	processing and metal concentrations and period of application (short
	term versus long-term) at four rates (ST treatments) up to EU limits
	for Zn, Cu and Cd in soils. All had the same amount of organic matter
	added. Wheat is grown and mineral fertilisers added every year. Samples
	of the sludge used and periodic soil and plant samples are available in
	the Rothamsted Sample Archive. This experiment is replicated on other
	soils in Gleadthorpe, Rosemaund, Bridgets and Hartwood.
</p>
<p>CS/428 and 439 are small plots (3.5 x 1.2 m) surrounded by boards for
	comparison of additions of the bulky ex-sewage works sludge in CS/427
	with respectively: a) small amounts of organic matter but the same
	amounts of metals in a specially made liquid sludge applied 1994-1997,
	or b) the same amounts of metals added as salts (no organic matter
	added).</p>

<p>Description provided by Professor Steve McGrath, Feb 2016, sponsor of
	these experiments.</p>


