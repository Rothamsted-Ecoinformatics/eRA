<A NAME="SEC12"></a>
<H2>Woburn Organic Manuring experiment</H2>

<div align="center">
	<img src="<?=$metadata?>Woburn_organic_manuring_675.jpg"
		title="Woburn Organing Manuring" width="675" height="230"
		alt="Woburn Organing Manuring" />
</div>
<p>
	<B>Current design:</B> A five year arable rotation (Wheat, Maize/C
	Crop, Rye, S.Barley/C Crop, Beans) with different organic amendments
	(FYM, Straw, Compost, None, permanent grass/clover leys). Total of 32
	plots divided into four blocks. Plots are split so that N can be
	applied at six rates for all crops, except beans which receive no N.
	Yields are recorded each year and soils are taken every 5 years. Total plot size
	8.83m x 8.0m Blocks 1, 2 and 4; 7.83m x 8.0m Block 3. N sub-plots are 4m wide. 

</p>

<p>
	<b>Strengths:</B> Cropping data, yields and soils available since 1964.
	The experiment is randomized and has adequate replication. It is the
	only long-term organic manure experiment on the sandy loam at Woburn
	and the only long-term experiment including cover crop treatments.
</p>

<p>
	<b>Background</B>: Started in 1964 on the sandy loam soil at Woburn to
	test the effects of different types of organic matter inputs on soil
	organic matter (SOM) and crop yields. Initially six organic treatments
	(FYM, peat, straw, green manures and two grass leys) were compared with
	two fertilizer-only treatments. Arable crops were grown in rotation
	with an eight-level N test 1973-1980 to assess the effects of the
	increased levels of SOM achieved by the organic amendments. During this
	period, no organic manures were applied. There was another treatment
	phase from 1981-1986, when further organic manures were applied. Again,
	SOM increased with the organic treatments and the grass leys but
	continued to decline slowly where only fertilizers were applied. This
	treatment phase was followed by another test phase, 1987-1994, when six
	rates of N were tested on arable crops, and no further organic manures
	were applied. From 1995-2002 arable test cropping continued but only
	two rates of N were tested. In 2003 another treatment phase started.
	All plots, except for the permanent grass-clover leys (and beans when
	grown) were split to test six rates of N on arable crops grown in
	rotation.
</p>


