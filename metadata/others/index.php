<H2>Continuous maize experiments, Rothamsted and Woburn</H2>

<div align="center">
	<img src="<?=$metadata?>Continuous_maize_hoosfield_675.jpg"
		title="Rothamsted Continuous Maize, Hoosfield" width="675"
		height="275" alt="Rothamsted Continuous Maize, Hoosfield" />
</div>
<p>
	<b>Design:</b> Continuous maize and barley plots with different amounts
	of maize tops incorporated. Total of 18 plots divided into three
	blocks. The experiment is duplicated at Rothamsted on a silty slay loam
	and Woburn on a sandy loam.
</p>

<p>
	<b>Strengths: </b>The experiment has good replication. The experiments
	provide a comparison between different soil types (silty clay vs sandy
	loam). Maize is incorporated every year so that it has contributed
	significantly to the soil organic C (SOC) stock The incorporation of C4
	maize residues provides an opportunity to measure the contribution to
	the SOC from the maize and evaluate its turnover using <sup>13</sup>C
	stable isotope techniques. The plots are large (225-300m<sup>2</sup>)
	so could be divided to include additional treatments.
</p>

<p>
	<b>Background:</b> The experiments were started in 1997, one at
	Rothamsted (Hoosfield) and the other at Woburn (Stackyard). There were
	six crop and straw treatments: 1. Continuous maize, stubble
	incorporated; 2. Continuous maize, stubble plus 10t maize tops
	incorporated; 3. Maize after three years of spring barley, straw
	removed; 4. Spring barley after five years maize, stubble incorporated;
	5. Continuous spring barley, straw removed plus 10 t maize tops
	incorporated; 6. Continuous spring barley, straw removed. Crop yields
	were taken each year and soils were collected in 1997, 2008 and 2015.
	Maize and spring barley were chosen as crops with contrasting delta <sup>13</sup>C
	enrichment in their residues. This can be used to follow the fate of
	the C incorporated in the crop residues. The experiments were
	established initially for use in SOM fractionation studies, but staff
	and analytical funds are no longer available to support this work, so
	the experiment was discontinued in 2015.
</p>

