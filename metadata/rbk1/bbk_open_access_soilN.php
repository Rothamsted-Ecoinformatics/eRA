<H1>Provenance and Quality</H1>
<P>The data contained in this spreadsheet are derived from measurements made at Rothamsted Research, Harpenden. The data presented is mean data from selected plots and sections only. Most of the chemical analysis was carried out by the  Rothamsted Research Analytical Unit, which uses standards and check samples to ensure quality control. Soil samples taken in 1881, 1893, 1914, 1936 and 1944 were re-analysed in 2001-4 for Total N by combustion (LECO). 
Data in 1865 are the original Soda Lime analyses from Dyer (1902), corrected to be equivalent to LECO. Data for 1966 and 1987 are by Kjeldahl analysis for Total N. Data for 1992, 1997, 2000, 2005 and 2010 are by combustion (LECO).  The complete data set, including the raw data used to derive the mean values, is available from the  <a href="mailto:era@rothamsted.ac.uk">e-RA Curators. </a> </p>

