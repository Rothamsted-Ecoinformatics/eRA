<H2>Description</H2>
<P>The figure shows the measured long-term changes in total % nitrogen concentration in the topsoil (0-23 cm) in selected treatments of
the Broadbalk experiment, where winter wheat has been grown most years since 1843 (&quot;continuous wheat&quot;).
Soil N concentration has remained almost constant in the unfertilized plot (nil), and the plot given PK since the start of the experiment. Inorganic fertilizer with nitrogen (N3PK) has enhanced soil N a 
little, probably due to increased returns of organic matter in crop roots, chaff and stubble.  There has been a slight decline in soil N on these plots in the last 20 years.
The treatment given FYM (35 t ha<sup>-1</sup>) since 1843 now has about three times the concentration of soil total N as the unfertilized plot. Increases due to FYM were greatest
in the initial years of the experiment. The same trends can be seen in the FYM treatments that started in 1885 and 1968.  The decline in soil N on the FYM plots in the 1920s and 1930s was because
FYM was not applied in the the years when the plots were fallowed to control weeds. The decline since 2000 on Plot 1 (FYM+N, 1968-2000) is because FYM has not been applied since 2000. </p>



<P><strong>CROPPING AND TREATMENTS</strong></p>
<P><strong>Continuous wheat:</strong> Wheat grown every year since autumn 1843, except when parts of the experiment were fallowed to control weeds. The experiment was divided into 10 sections in 1968; 
data shown are mean of sections 1, 6 and 9, which are those sections in continuous wheat (except for occasional fallow). Plot 1 data is from section 6 only (there is no plot 1 on sections 1 and 9).
In other sections, not included in this chart, wheat is grown in rotation with other crops.</p>
<P><strong>Nil</strong>: Plot 3 - No fertilizer or manure applied since 1852.<br />
<strong>PK:</strong> Plot 5- PKMg applied since 1852 (P withheld since 2001).<br />
<strong>N3PK</strong>: Plot 8 - PKMg + 144 kgN ha <sup>-1</sup> applied since 1852 (P withheld since 2001). <br />
<strong>FYM since 1843:</strong> Plot 2.2 - 35 t ha <sup>-1</sup> of farmyard manure applied since 1843.<br />
<strong>FYM since 1885 + N since 1968</strong>: Plot 2.1 - 35 t ha <sup>-1</sup> of farmyard manure since 1885 plus 96 kgN ha <sup>-1</sup> since 1968, plus 144 kgN ha <sup>-1</sup> since 2005.<br />
<strong>FYM+N 1968-2000; N4 since 2001</strong>: Plot 1 - 35 t ha <sup>-1</sup> of farmyard manure 1968-2000 only, plus PK + 96 kgN ha <sup>-1</sup> 1968 -1984, plus PK + 192 kgN ha <sup>-1</sup> 1985-2000, 192 kgN only since 2001.</p>
<P><br />
  <strong>AGRONOMIC PRACTICES</strong></p>
<p><strong>Fallowing:</strong> Between 1926 and 1967 the experiment was divided into five sections which were bare fallowed sequentially one year in five to control weeds. Wheat was grown in the other four years.<br /><strong>Liming:</strong> Lime (calcium carbonate, often referred to as chalk) has been applied since the 1950s to maintain soil pH at a level which does not limit yield.<br /><strong>Herbicides:</strong> Herbicides were introduced in 1964; previously weeds were controlled by hand-hoeing or by fallowing and cultivation.<br /><strong>Modern cultivars:</strong> Modern short-strawed, high-yielding cultivars since 1968.<br /><strong>Fungicides:</strong> spring and summer fungicides as necessary since 1978</p>
