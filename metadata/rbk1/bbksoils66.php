<H1>1966 Soil Sampling, analysed 1966-67</H1>
<P>
<BODY BGCOLOR="#ffffff">

<p>
In 1966 the surface soil of all plots was sampled with a semi-cylindrical auger, (2cm diameter), to a depth of  23cm.  Twenty cores were taken from individual plots within each
section.  To avoid areas where soil may have been mixed by cultivation, only the central third of each plot was sampled.   </p>

<p>Samples were air-dried, sieved &lt 2mm and a sub-sample ground &lt 0.5 mm.  Determination of pH, NaHCO<SUB><FONT SIZE = -1>3</FONT></SUB>-soluble P, exchangeable cations were carried out on &lt
2mm sample; organic C and total N on &lt 0.5 mm sub-sample. </p>
