<H1>1987 and 1988 Soil Sampling</H1>



<BR>
Surface soil samples (0-23cm) from individual plots within sections 1, 4, 7 and 9 in 1987 and sections 0, 2, 3 and 5 in 1988.  Twenty cores taken from each plot.  To avoid areas where soil may have been mixed by cultivation a 1m strip down each side of the plot was not sampled.<P>

Samples were air-dried, sieved &lt 2mm and a sub-sample ground &lt 0.5 mm.  Determination of pH, NaHCO<SUB><FONT SIZE = -1>3</FONT></SUB>-soluble P, exchangeable cations were carried out on &lt 2mm sample; organic C and total N on &lt 0.5 mm sub-sample.
<P>

