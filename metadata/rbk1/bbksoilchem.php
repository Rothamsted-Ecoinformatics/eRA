<h2>Analysis methods for soil chemical properties </h2>

<p>All soil samples are air-dried and sieved <2mm.  Data are given for air-dried soil (approximately 98% dry matter). When calculating total amounts in the soil (e.g. kg N ha<sup>-1</sup> )
you may wish to convert to oven-dry soil (i.e. 100% dry matter). Broadbalk <a href="http://www.era.rothamsted.ac.uk/Broadbalk/bbksoils#SEC4" target="_blank"> <b>standard soil weights</b> </A> 
are given for oven-dry soil.  </p>

<h3>Total soil % nitrogen (%N)  </h3>
<p><b>1865:</b> Original soda lime analysis for total N (Johnston, 1969b, table 5.10) multiplied by a factor derived from the comparison of soda lime and LECO analysis 
values for 1881 and 1893 samples. Soda lime analysis by the method of Will and Varrentrapp (Watt, 1863). See Johnston (1969a, p 50) for more details.   </p>
<p><b>1881-1944:</b> Selected samples re-analysed in 2001-4 by combustion analysis, based on the Dumas method, using a LECO combustion system. Measured on air-dried, 
finely ground soil (to pass a 355 micron or 44 mesh sieve).   </p>
<p><b>1966, 1987-8:</b> Kjeldahl digest method for total N (Bremner, 1965). The digest was then analysed colorimetrically using a Technicon continuous flow analyser. 
Measured on air-dried soil, finely ground to pass a 0.5mm sieve.  </p>
<p><b>1992 onwards:</b> Combustion analysis, based on the Dumas method, using a LECO combustion system.  Measured on air-dried, finely ground soil (to pass a 355 micron 
or 44 mesh sieve).   </p>


<h3>Soil % organic carbon (%SOC)</h3>
<p><b>1865:</b> Derived from original soda lime analysis for total N and C:N ratios for 1893 for organic carbon (Dyer, 1902).    </p>
<p><b>1881-1944:</b> Selected samples re-analysed in 2001-4 by combustion analysis, based on the Dumas method, using a LECO combustion system to measure total carbon. 
SOC determined as total C minus CaCO<sub>3</sub>-C, measured by a calcimeter (see below). Measured on air-dried, finely ground soil (to pass a 355 micron or 44 mesh sieve).  </p>
<p><b>1966:</b> Chromic acid titration method (Walkley and Black, 1934). Correction factor of W-B x 1.3 used, which is equivalent to organic C by Tinsley or total C by combustion
minus CaCO<sub>3</sub>-C. But see also Johnston (1969b, p 97). Measured on air-dried soil, ground to pass a 0.5mm sieve. </p>
<p><b>1987-8:</b> Dichromate digestion, modified Tinsley (Kalembasa and Jenkinson, 1973) to measure organic C.  Measured on air-dried soil, finely ground to pass a 0.5mm sieve.       </p>
<p><b>1992 onwards:</b> Combustion analysis, based on the Dumas method, using a LECO combustion system to measure total C. Measured on air-dried, finely ground soil 
(to pass a 355 micron or 44 mesh sieve). SOC determined by subtraction of CaCO<sub>3</sub>-C, measured by a calcimeter (see below). </p>

<h3>Inorganic carbon (IC) also known as calcium carbonate-C or CaCO<sub>3</sub>-C </h3>
<p>Soils sampled in 1865-1936 were re-analysed in 2000-4. Most of the 1944 data was from the 1944 analysis, except for a few plots (9, 18 and 19) that were analysed in 2001.
IC was not measured in 1987-88, as %SOC was determined directly by Tinsley analysis.   In other years IC is subtracted
from total carbon to give %SOC.  </p>
<p>All samples up to 2012 were analysed by a calcimeter.   CO<sub>2</sub> is liberated from CaCO<sub>3</sub> in the soil sample by treating with hydrochloric acid (HCl) in a closed system.
The amount of CaCO<sub>3</sub> is calculated by comparing the pressure produced by the sample against the pressure produced by known weights of CaCO<sub>3</sub>, using a mercury 
filled manometer. %CaCO<sub>3</sub>-C or %IC is derived from %CaCO<sub>3</sub> by dividing by 8.333. Since 2014 inorganic C has been measured by an automated Skalar Primacs
inorganic carbon analyser.  For more details, see <a href="http://intranet.rothamsted.ac.uk/analytical-unit/notes-papers" target="_blank"><b>Rothamsted Analytical Unit notes.</b> </A>

<h3> Soil pH</h3>
<p>Soil pH in water, with a 1:2.5 soil:water suspension, mean of two readings. Soils from selected treatments sampled in 1865-1944 were re-analysed in 2000-4. Measured on air-dried soil, 
sieved < 2mm. </p>

<h3>Olsen-P (plant-available phosphorus; also known as bicarbonate soluble-P or NaHCO<sub>3</sub>-soluble P)</h3>

<p>Soils from selected treatments sampled in 1865-1944 were re-analysed in 2000-4. All samples were analysed by the Olsen method, in which soil is extracted with a solution 
of 0.5<i>M</i> NaHCO<sub>3</sub>,
buffered at pH 8.5 (Olsen et al, 1954). The extract is then analysed by continuous segmented colorimetric flow analysis.  Measured on air-dried soil, sieved < 2mm. </p>
<p> See Blake et al (2000, 2003) for a discussion of the P balance on Broadbalk and changes in soil P fractions over time (Key References, below). </p>

<h3>Exchangeable cations -  Calcium (Ca), magnesium (Mg), potassium (K) and sodium (Na)</H3>
 <p>Selected soils sampled in 1865-1944 were re-analysed in 2000-6, and those sampled in 1966 were re-analysed in 2019. All soils were extracted with 1<i>M</i> ammonium acetate 
 (NH<sub>4</sub>CH<sub>3</sub>CO<sub>2</sub>) solution, after the method of Metson (1956).
 The data is expressed as mg kg<sup>-1</sup> in dry matter, with 5mg of soil in 100ml of leachate.
 Since 1983, the extracts have been analysed by ICP-OES (Inductively Coupled Plasma - Optical Emission Spectrometer). Measured on air-dried soil, sieved < 2mm. </p>
<p>Blank (control) values: Analysis blanks were subtracted from exchangeable Na, and other cations if necessary (e.g. K in 1987).        </p>

<p>Samples taken in <b>1966</b> were re-analysed in 2019. The exchangeable K values differ from those published by Johnson (1969b), when a slightly different technique was used for measuring
exchangeable cations, which involved swirling the samples in successive amounts of ammonium acetate and decanting off the solution once settled. In 1966 the extracts were measured with a
flame photometer.  Bolton (1972) reports on exchangeable Ca and Mg in Broadbalk soils from 1856-1966 for some sections, from the western (top) end of the field, but the 1966 data has not been found.
It is recommended that the re-analysed data from 2019 is used for the 1966 samples.  </p>



<p>For more details of current analytical methods, see <a href="http://intranet.rothamsted.ac.uk/analytical-unit/notes-papers" target="_blank"><b>Rothamsted Analytical unit notes.</b> </A>    </p> 
<p>See Bolton (1972) for changes in exchangeable Ca and Mg, 1856-1966 and Blake et al (1999) for a study of soil K content, crop K uptake and K balance in Broadbalk (Key References, below). </p>
