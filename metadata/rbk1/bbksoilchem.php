<h2>Data available </h2>

<p><b>Soil chemical properties</b> have been measured at regular intervals on Broadbalk since 1865, in topsoils (0-23cm) and subsoils. 
See <a href="<?php echo $metadata ; ?>Measurements of Broadbalk soil properties 1843-1944.pdf" target="_blank">soil measurements 1843-1965 </A> and 
<a href="<?php echo $metadata ; ?>Measurements of Broadbalk soil properties 1966-2017.pdf" target="_blank">soil measurements 1966-2017 </A> for details of what data is available.  

Not all plots, sections or soil depths
have been measured every year. The following soil chemical properties have been measured:</p>
<UL>
<li>%N : total soil Nitrogen % </li>
<li>%SOC : soil organic Carbon % </li>
<li>Olsen P : plant-available phoshorus, by extraction with a solution of 0.5<i>M</i> NaHCO<sub>3</sub>, buffered at pH 8.5</li> 
<li>pH : soil pH in water (1:2.5 soil : solution) </li>
<li>Exchangeable cations : exchangeable Ca, K, Mg and Na by extraction with 1<i>M</i> ammonium acetate solution</li>
<li>% inorganic carbon (CaCO<sub>3</sub>-C) : by calcimetry </li>
<li>soil weights : see <a href="http://www.era.rothamsted.ac.uk/Broadbalk/bbksoils#SEC4" target="_blank"> <b>soil physical properties</b> </A> for details </li>
