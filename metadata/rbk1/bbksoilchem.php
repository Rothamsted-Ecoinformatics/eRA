<h2>Soil Sampling </h2>
<p>Broadbalk soil has been sampled on many occasions over the years. However, because the method of sampling has changed and the experiment has been 
divided, first into two halves, then into five sections and finally into 10 sections, it is not always advisable to directly compare one sampling with another without 
careful thought. This <a href="<?php echo $metadata ; ?>Soil sampling; comparisons over time to 2017.pdf" target="_blank"> <b>table</b> </A> shows, where possible,
how samples taken over time relate to each other. Selected plots and depths have been sampled on other occasions;  most samples still exist in the Sample Archive. </p>

<p>Since 1992 a systemic sampling plan has been adopted. In 1992, 1997, 2005, 2010 and 2015 all five continuous wheat sections were sampled (0, 1, 6, 8 and 9). 
In the intervening years the remaining sections in rotation were sampled, one per year, so that all sections were sampled every five years. All sections were sampled in 2000 prior to 
treatment changes being introduced.     See <a href="<?php echo $metadata ; ?>Soil sampling; comparisons over time to 2017.pdf" target="_blank"> <b>table</b> </A> for full details.</p>

<p> In autumn 2004 it was apparent that parts of the field had been ploughed slightly deeper than 23cm as sub-soil clay was visible in random patches across the field. Thus 
in 2005 and 2006 all plots were resampled to create a new baseline, if necessary, for soil chemical properties. The sections were then sampled systematically from 2008 onwards.
</p>
<p>In 2000-2004 archived soil samples from selected plots sampled in 1865, 1881, 1893, 1914, 1936 and 1944 were re-analysed for soil pH, Olsen-P, exchangeable cations, Total %N,
%SOC and CaCO<sub>3</sub>-C. Values for %N and %SOC from the re-analysis of the 1865 samples were very different to the original data and the 1881 and 1893 re-analysed soils,
so the original data was used. </p>

<P>Data before 1926 (when the experiment was divided into sections) are given for whole plots only, from the re-analysis in 2000/2004. More
recent data are available for individual plots within each section. </P>

<p><b>1936, 1944 and 1966</b> data is included with later data, although the experiment was not divided into 10 sections until 1968. </p>

<p><b>1936 data: </b>10-20 cores taken with a narrow auger from each of the five Old Sections were bulked within each plot. Data for Old Section I is used for current sections 0 and 1, 
Old Section II for current sections 2 and 3, etc. (see soil sampling plan for more details). All the data is from the re-analysis of old samples in 2001-2002. </p>

<p><b>1944 data: </b> four holes were taken from each of the (then) five Old Sections; holes 1 & 2 were on current Section 0, holes 3 & 4 on current Section 1 etc, 
so the data can confidently be allocated to the modern sections.   Data is presented as the mean of the two holes for the topsoil.   Subsoil data is not available for each modern section, 
as the samples were bulked from four-sub-samples for each of the five old sections.  Data for Old Section I is used for current sections 0 and 1, 
Old Section II for Sections 2 and 3, etc. (see soil sampling plan for more details).  The soil was re-analysed in 2003-5 from selected plots from all Sections and depths. 
All data is from the re-analysis except %CaCO<sub>3</sub> which was analysed in 1944.  Soil pH and Exchangeable Na were not measured in the 1944 subsoil samples. </p>

<p><B>1966 data: </b>Section 0 and 1 use data from Old Sections Ia and Ib respectively;
Sections 2 and 3 use data from Old Section II; Sections 4 and 5 use data from Old Section III; Sections 6 & 7 use data from Old Section IV; Sections 8 and 9 use data from Old Sections 
Va and Vb respectively. Bulked Va and Vb samples (ie Sections 8 and 9) were used to determine %SOC (plots 2.1 and 2.2 only) and for all plots for Olsen P, %CaCO3 and Exchangeable K. 
There was no plot 1 in 1966, this was created in 1968.  The soil was sampled in September 1966 but no day is given. It is shown as 15/09/1966 in the database.   </p>
<p>Data from the samplings in 2001-2004 is not included as this only covered sections 2, 4, 5 and 7 and there was a comprehensive sampling of 
all sections in 2000 and then in 2005/6.</p>


<h3>Soil Sampling Methods</h3>
<p>Samples between <b>1865 and 1914</b> were taken with an open-ended metal box, 9 inches (23cm) deep and usually 6 x 6 inches (15 x 15cm) across. There were between three and eight sample positions
on each plot which were bulked together for each depth on each plot.  In <b>1944</b> a spade was used to sample the  0-23cm layer, and the subsoil (23-46cm) was sampled with a semi-cylindrical
auger. </p>
<p>In <b>1936</b> and from <b>1966 onwards</b> samples were taken with a semi-cylindrical auger. 10-20 cores were taken from the different soil layers for each individual plot within
each section and bulked together for each plot. Small diameter cores taken by semi-cylindrical augers cannot be used to determine soil weights, but provided enough are taken the 
sample better represents the proportions of SOC, N, P, K etc in the soil than a few large box samples.
See <a href="http://www.era.rothamsted.ac.uk/Broadbalk/bbksoils#SEC4" target="_blank"> <b>soil physical properties</b> </A>for details of soil weights.  </p>


<p>Samples were taken in the autumn, after the crop had been removed, but before ploughing, except for Section 3, 1996 which was sampled in March. </p>
<P>See below for sampling details for specific years. See also Johnston (1969b) in Key References
and <a href="<?php echo $metadata ; ?>Soil sampling; comparisons over time to 2017.pdf" target="_blank"> <b>sampling table</b> </A>for more details. </p>

