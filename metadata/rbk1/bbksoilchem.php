</p>
<p><b>Soil pH:</b> The plough layer (0-23 m) is limed when necessary to maintain a minimum soil pH of 7.0 – 7.5.
Broadbalk was first limed regularly from 1955-1967, with plots given the larger inputs of ammonium fertilizers receiving more lime than the controls. Liming stopped from 1968-1975, 
but began again in 1976-1992, with 3 or 4 sections being limed each year. From 2007 onward selected plots have been limed every 5-6 years based on soil pH measurements to maintain top-soil 
pH around 7.0-7.5. Selected plots were limed in autumn 2018. </p>

