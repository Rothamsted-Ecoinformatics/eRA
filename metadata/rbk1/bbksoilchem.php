<H1>Broadbalk soil chemical properties</H1>
<p>The Broadbalk wheat experiment, established in the autumn of 1843, has  been cultivated since at least 1623, and probably much earlier (Avery &amp; Bullock, 1969). 
Soil chemical properties have been measured at regular intervals since 1865.  </p>
