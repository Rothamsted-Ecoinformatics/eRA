<h1>Description</h1>
<p>The figure shows the long-term changes in soil organic carbon (SOC) (t ha<sup>-1</sup>) content in the topsoil (0-23 cm) in selected treatments of
  the Broadbalk experiment, where winter wheat has been grown each year since 1843 (&quot;continuous wheat&quot;). Data has been calculated from measured % SOC (0-23 cm) and standard
  soil weights, adjusted for observed decreases in top soil bulk density of plots given FYM, by including the appropriate amount of subsoil to ensure 
  soil weights were comparable over time. SOC has remained almost constant in the unfertilized plot, at the equilibrium level for this farming system 
  on this soil type. Inorganic fertilizer (N3PK) has enhanced SOC a little, probably due to increased returns of organic matter in crop roots and above-ground crop residues. 
  The treatment given FYM (35 t ha<sup>-1</sup>) since 1843 now contains almost three times as much SOC as the unfertilized plot. Increases due to FYM were greatest 
  in the initial years of the experiment. The same trends can be seen in the FYM treatments that started in 1885 and 1968.</p>
<p><strong>CROPPING AND TREATMENTS</strong></p>
<p><strong>Continuous wheat:</strong> Wheat grown every year since autumn 1843, except when parts of the experiment were fallowed to control weeds. The experiment was divided into 10 sections in 1968; data shown are mean of sections 1, 6 and 9, which are those sections in continuous wheat (except for occasional fallow). In other sections, not included in this chart, wheat is grown in rotation with other crops.</p>
<p><strong>Nil</strong>: Plot 3 - No fertilizer or manure applied since 1852.<br />
  <strong>PK:</strong> Plot 5- PKMg applied since 1852.<br />
  <strong>N3PK</strong>: Plot 8 - PKMg + 144 kgN ha <sup>-1</sup> applied since 1852. <br />
  <strong>FYM since 1843:</strong> Plot 2.1 - 35 t ha <sup>-1</sup> of farmyard manure applied since 1843.<br />
  <strong>FYM since 1885 + N since 1968</strong>: Plot 2.2 - 35 t ha <sup>-1</sup> of farmyard manure since 1885 plus 96 kgN ha <sup>-1</sup> since 1968, plus 144 kgN ha <sup>-1</sup> since 2005.<br />
  <strong>FYM+N 1968-2000; N4 since 2001</strong>: Plot 1 - 35 t ha <sup>-1</sup> of farmyard manure 1968-2000 only, plus PK + 96 kgN ha <sup>-1</sup> 1968 -1984, plus PK + 192 kgN ha <sup>-1</sup> 1985-2000, 192 kgN only since 2001.</p>
