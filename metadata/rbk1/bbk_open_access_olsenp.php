<H2>Description</H2>
<P>The figure shows changes in plant-available phosphorus (Olsen P) in the topsoil (0-23cm) of selected plots of the Broadbalk Wheat experiment.
Olsen P, which is commonly used to assess plant-available P, is measured by extracting soil with a solution of 0.5M NaHCO3, buffered at pH 8.5. Until 2000, most of the Broadbalk plots had received superphosphate P (35 kg P ha<sup>-1</sup>) each year since 1852. Since 2001, applications of P have been withheld on some plots. The accumulation of available P was greatest on the FYM plot, reflecting the larger total P inputs in FYM (approx 45 kg P ha<sup>-1</sup>) compared to that applied as inorganic fertilizer or where no P was applied. The greater Olsen P concentrations on plot 5 (PKMg) compared to plot 8 (N3PKMg), despite their similar P inputs, were a result of the higher yields and greater P offtakes in the crop on plot 8, due to the applied N. Plot 18 (N4 1/2PKMg) received P at half the standard rate between 1852 and 2001, resulting in relatively low Olsen P concentrations. Since 2001 fertilizer P has been applied at the full rate on this plot, and there has been a corresponding increase in Olsen P. Olsen P concentrations were least on plots receiving no (or very little) P inputs in fertilizer (plot 3, given no fertilizer or manure). Since 2001, fertilizer P has not been applied to plots 5 and 8 (and others not shown), due to the high reserves of plant-available P in the soil. This is reviewed each year.</p>
<P><strong>CROPPING AND TREATMENTS</strong></p>
<P><strong>Continuous wheat:</strong> Wheat grown every year since autumn 1843, except when parts of the experiment were fallowed to control weeds. The experiment was divided into 10 sections in 1968.<br />
<strong>1st wheat in rotation:</strong> Some parts of the experiment have been in crop rotations since 1968. <br />
<strong>Data shown:</strong>  mean of all sections, both continuous wheat and wheat in rotation. <br />
<strong>Nil</strong>: Plot 3 - No fertilizer or manure applied since 1852. Originally two half plots 3 and 4; 4 received P from 1843-51.<br />
<strong>N4:</strong> Plot 10 - N2 only from 1852-2000; N4 since 2001.<br />
<strong>PKMg</strong>: Plot 5 - PKMg from 1852-2000; P withheld since 2001 due to high soil reserves.<br />
<strong>N3PKMg:</strong> Plot 8 - N3 PKMg from 1852-2000; P withheld since 2001 due to high soil reserves.<br />
<strong>N4 + 1/2PKMg</strong>
: Plot 18 - N2 or PKMg  in alternate years from 1852-1967 (i.e. half rate of P); N2 1/2[PKMg] from 1968-1984; N1+3 1/2[PKMg] from 1985-2000; N1+2+1 PKMg since 2001 (i.e. full rate of P since 2001).<br />
<strong>FYM since 1843</strong>: Plot 2.2 - farmyard manure 35 t ha<sup>-1</sup> since 1843.<br />
</p>
<P><strong>AGRONOMIC PRACTICES</strong></p>
<P><strong>Fallowing:</strong> Between 1926 and 1967 the experiment was divided into five sections which were bare fallowed sequentially one year in five to control weeds. Wheat was grown in the other four years. <br />
<strong>Liming:</strong> Lime (calcium carbonate, often referred to as chalk) has been applied since the 1950s to maintain soil pH at a level which does not limit yield.<br /><strong>Herbicides:</strong> Herbicides were introduced in 1964; previously weeds were controlled by hand-hoeing or by fallowing and cultivation.<br /><strong>Modern cultivars:</strong> Modern short-strawed, high-yielding cultivars since 1968.<br />
<strong>Fungicides:</strong> spring and summer fungicides as necessary since 1978.</p>
<P><strong>ESTIMATED STARTING VALUE IN 1843</strong></p>
<P>There is an estimated starting value of 10 mg kg<sup>-1</sup>  although it is unlikely that all treatment strips would have started at the  same value. Broadbalk had been unmanured for five years before the experiment  had started in 1843. 
Prior to that it is unlikely to have had large inputs of  fertiliser or manure so it is probable that plant-available P would have been  relatively low. A measured value for the start of the nearby Exhaustion Land  Experiment in 1856 was 10 mg kg<sup>-1</sup>.</p>
