<H2>Soil miosture and drainage</H2>

<p><b>Soil Moisture Characteristics:</b> With nearly 200 plots in the experiment, there isn’t water retention information for all treatments. Water retention characteristics for contrasting plots can be found in Salter & Williams (1969).
For more recent information, see Gregory <i>et al</i>, 2010, where retention characteristics have been fitted to the van Genuchten model. </p>

<P><b>Soil Drainage:</b>
Lawes described the soil of Broadbalk as having good natural drainage. However, as it was an experimental field, it was decided to improve the drainage, to allow 
greater access. Tile drains were installed under each strip in autumn 1849 (except plot 20). They were installed in the centre of the strip, under the furrow separating the 'a' and 'b' halves (see Plan 1852-1925). Tiles drains were installed under strip 2.1 (2A) in autumn 1884 before the first application of FYM. Tile drains were 5cm in diameter and 60-76 cm deep, and they discharged into a 10cm diameter main drain at the east side of the field. <p>
<P>Lawes and Gilbert realised that the drains could be used to measure losses of plant nutrients from the different fertilizer treatments. Small outlet pits were dug at the 
intersection of each strip drain and the main drain in December 1866 to sample the water draining from strips 2 to 16. This was not ideal, as there was the risk of the samples being contaminated. The drains from strips 17-19 were opened in November 1878. In spring 1879 the collection of drainage water was improved, with the drains from each strip discharging into their own pit, which overflowed into a separate, deepened main drain which was kept open. In 1897/98 this main drainage ditch 
was enlarged, the base concreted and the sides bricked. (Details from Johnston & Garner, 1969).  </p>


<A NAME="SEC3"></a>
<h2>Soil description</h2>
<UL>
<li> FAO Classification:	Chromic Luvisol  (or Alisol)</li>
<li>U.S. Soil Taxonomy: 	Aquic  (or Typic) Paleudalf</li>
<li>Soil Survey of England & Wales Group: Stagnogleyic paleo-argillic brown earth (Avery, 1980) </li>
<li>Soil Survey of England & Wales Series:  Predominately Batcombe Series (Avery & Catt, 1995 – see Soil Map link on left hand side).
</UL> 
<p>For more details of the Batcombe and other soil series, see  <a href="http://www.landis.org.uk/services/soilsguide/series_list.cfm" target="_blank"> Cranfield University 2018 Soils Guide.</a>  </p>
<p>
<b>Soil texture class: </b> Clay loam to silty clay loam over clay-with flints. The soils contain a large number of flints and are slightly calcareous. Below about 2m depth the soil becomes chalk. The experiment is under-drained and the site is free draining. </p>

<p><b>Soil texture,</b> 0-23cm (from Gregory <i>et al</i>, 2010)</p>
<ul>
<li>Sand (2000 - 63 &micro;m) :	25 %</li>
<li>Silt (63 - 2 &micro;m) :	50 %</li>
<li>Clay (<2 &micro;m) :	25 %</li>
<li>Particle density g cm <SUP><FONT SIZE = -2>-3</FONT></SUP> :	2.56 %</li>
</UL>
<p>
There is considerable variation in soil texture across the site, with clay contents ranging from 19 – 39% (Watts <i>et al</i>, 2006). The mean clay content for Sections 0 (straw incorporated since 1986) and 1 (straw removed, both continuous wheat) is 28.3% (Watts <i>et al</i>, 2006). Clay content increases with soil depth in the Batcombe Series (from Jenkinson <i>et al</i>, 2008):
<UL>
<li>23-46 cm :	30 % clay</li>
<li>46-69 cm :	50 % clay</li>
<li>69-92 cm :	49 % clay</li> 
</UL>
</p>
<p><b>Soil pH:</b> The plough layer (0-23 m) is limed when necessary to maintain a minimum soil pH of 7.0 – 7.5.
Broadbalk was first limed regularly from 1955-1967, with plots given the larger inputs of ammonium fertilizers receiving more lime than the controls. Liming stopped from 1968-1975, but began again in 1976-1992, with 3 or 4 sections being limed each year, with 2.9t/ha of chalk being applied each autumn to all plots of each section. From 2007 onward selected plots have been limed every 5-6 years based on soil pH measurements to maintain top-soil pH around 7.0-7.5. Selected plots were limed in autumn 2018. </p>

<p>For information on soil organic carbon, see  <a href="Broadbalk/bbk_open_access#SEC2" target="_blank"> SOC Open Access dataset.</a></p>

<p>For information on soil concentration of Olsen P (plant-available P), see  <a href="Broadbalk/bbk_open_access_olsenp" target="_blank"> Olsen P Open Access dataset.</a></p>




