<H1>Broadbalk site details and soil physical properties</H1>
<p>The Broadbalk wheat experiment, established in the autumn of 1843, has  been cultivated since at least 1623, and probably much earlier (Avery &amp; Bullock, 1969). 
In his first Rothamsted paper, published in 1847, Lawes described the soil as &quot;a heavy loam resting upon chalk, capable of producing good wheat when well manured&quot; . 
Here, details are given of the site, the soil description and standard soil weights.</p>
