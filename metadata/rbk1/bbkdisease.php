<H1>Broadbalk Diseases</H1>
<p>
The following wheat root and stem diseases have been assessed on selected plots regularly since the 
introduction of rotations in 1968: 
</p>
<ul>
<li> <b>Take-all</b>  <i>Gaeumannomyces graminis</i> (Sacc) Arx & Oliver var.<i>tritici</i></li>
<li> <b> Eyespot</b> <i>Oculimacula acuformis</i> and <i>Oculimacula yallundae</i></li>
<li> <b>Sharp eyespot </b>  <i>Rhizoctonia cerealis </i></li>
<li> <b>Brown foot rot </b> <i> Fusarium spp.</i> </li>
<li class="nicelist"><A href="<?php echo $request; ?>#papers">Key References</A> referring to Disease</li>
</ul>

<p>

The <b>take-all</b> fungus infects winter wheat roots in the autumn causing black necrotic lesions on the roots. 
Early infections can lead to uneven growth in the spring and occasionally plant death. If severe infection
 occurs in June/early July above ground symptons often show as patches of premature ripening plants with reduced 
yields and grain quality. Comparisons of yields and of differences in amounts of take-all between continuous 
wheat on Broadbalk and wheat in other fields growing shorter sequences of cereals culminated 
in the development of the hypothesis of  'take-all decline'. This phenomenon is now widely recognised
and has been shown to occur in all the susceptible cereals. </p>

<p><b>Eyespot </b>was first identified in the UK in 1935 on Broadbalk. It is common on intensively cultivated
 cereals in heavy soils and is favoured by long wet and cold periods in winter and spring.  The fungus
 can survive on infected stubble and in the soil for 2-4 years. Early symptoms are difficult to assess and can
 range from a brown smudge to a typical eye shaped lesion. Careful removal of the outer leaf sheath can reveal a small
 black dot, the penetrating stroma. It penetrates the leaf sheath eventually infecting the stem. Severe infection 
softens the stem, often showing as white heads and can lead to lodging of the crop as it matures.
</p>


<p><b>Sharp eyespot</b> causes numerous stem and stem base lesions. The fungus overwinters primarily
 as mycellium on infected stubble but has a large host range which can also act as a source of inoculum. As a 
result, the fungus is not easily controlled by rotation. Early symptoms show as well defined lesions on 
the outer leaf sheaths and frequently have interveinal tissue shredding within the lesion. Infection can 
occur at any time during the growing season. Late infections often remain on the leaf sheaths but early 
infections can penetrate the straw often causing multiple lesions that can be observed as far up the stem
 as the 4th node. Severe infection can cause white heads and make the straw brittle. The disease is favoured
 by cold and dry conditions and is less prevalent in intensively cultivated cereals.

  </p>

<p><b>Brown foot rot</b> is caused by <I>Fusarium spp.</I> and is both soil and seed-borne. Infection
 often begins at the base of the leaf sheath and spreads up the leaf. The fungus continues to spread
 eventually reaching the nodes and straw causing two distinct symptoms. An overall general browning,
 mainly attributed to <i>Fusarium culmorum</i>, and streaky brown lesions caused by <i>Microdochium nivale</i>.
Severe <i>Fusarium</i> infection can result in whiteheads and occasionally lodging of the crop. 



<p><b>Measurements</b></p>

<p>Take-all, eyespot, sharp eyespot and brown foot rot have been routinely assessed on Broadbalk on Sections 9
 and the 1st, 2nd and 3rd wheats of the rotational sections since 1968. Several plots were assessed each year 
and from 1985 mainly plots 7 (N2PKMg), 10 (N2), 11 (N2P), 13 (N2PK), 15 (N5PKMg) and 21 (FYMN2). Disease
 assessments were generally carried out in late June/early July, at growth stage 69-77. Data in e-RA is 
currently available from 1968 - 2009.
</p>


<p>No disease assessments were done from 1982-1984. This was a transition period during which the rotation 
period changed from 3 to 5 years.</p>

<p>The percentage of plants with slight, moderate and severe <b>take-all</b> infection is assessed as follows:
   0 = no infection (healthy); 1 = < 25% of the root system infected (slight take-all); 2 = 25-75% infected 
(moderate take-all); 3 = > 75% roots infected (severe take-all). A take-all rating (TAR) is determined 
(Gutteridge et al., 2003). It is calculated as follows: (1x % plants with slight infection) + (2 x % plants with
 moderate infection) + (3 x % plants with severe infection). The TAR is a measure of take-all intensity with 
a range from 0 (no take-all) to 300 (severe infection on all plants).</p>

<p>The severity of infection by <b>eyespot</b> is assessed as described by Scott & Hollins  (1974). Plants
 with slight eyespot have small lesions occupying less than half of the circumference of the straw. Plants
 with moderate eyespot have lesions occupying more than half of the circumference of the straw. In plants 
with severe eyespot the straw is completely girdled by lesions and/or tissue softened.</p>


<p>Symptoms of <b>sharp eyespot</b> are often very superficial and a distinction is made only between symptoms
 that are slight, small lesions occupying less than half of the circumference of the straw or severe where
lesions occupy more than half of the circumference of the straw and usually extend over more than one internode
 (Goulds & Polley, 1990). </p> 


<p><b>Brown foot rot</b> is classified as slight or severe (Goulds & Polley, 1990). Plants with slight brown
 foot rot have either a general light browning occupying most of the circumference of the straw (typically
 caused by <i>Fusarium culmorum</i>) or fewer than 5 dark brown-black narrow streaks c.1mm wide (typically caused
 by <i>F. nivale</i>). Severe symptoms are defined as a general dark brown discoloration of the straw usually extending
 over more than one internode (<i>F. culmorum</i>) or more than 5 brown-black streaks greater than 1mm wide (<i>F. nivale</i>).
</p>

