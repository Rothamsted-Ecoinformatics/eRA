<H2>Description</H2>
<P>

This summary data shows the mean long-term winter wheat yields from selected treatments on Broadbalk 1852-2016, excluding spring wheat in 2015. These changes in mean long-term winter wheat yields reflect the improved treatments and agronomic practices introduced on Broadbalk such as modern cultivars, better
  control of pests, diseases and weeds, especially since the 1960s.  To control weeds, the  experiment was divided into five sections in the 1920s and one section bare  fallowed each year. The introduction
  of herbicides removed the need for  fallowing. Yields of continuous wheat given no fertilizer or manure have remained  at around 1 t ha<sup>-1</sup>.
  In 1968 a rotation was  introduced on part of the experiment, so that it is now possible to compare the  yields of wheat grown continuously and as the first wheat after a two-year  break. 
  Since 1979 summer fungicides have been used, which has allowed us to exploit the greater grain  yield potential of modern cultivars. From 1985, two higher N rates have been tested, 240 and 288 kg N ha <sup>-1</sup>. 
  The highest yields are now from the first wheat crop in
  rotation, with the  greatest yields from fertilizer alone exceeding those from FYM alone, and the  combination of FYM + 96 kgN ha<sup>-1</sup> (144 kgN ha<sup>-1</sup> since 2005) often exceeding 
both. The largest annual wheat yields on Broadbalk (&gt;13 t ha<sup>-1</sup>) were recorded in 2014, following the change in variety from Hereward to Crusoe. The greatest yields were not always achieved with the highest N rate. The figure shows the mean greatest first wheat yields achieved from the NPK treatments, receiving up to a maximum of 288 kg N ha<sup>-1</sup> (a maximum of 192 kg N ha<sup>-1</sup> from 1968-1984).</p>
<P><strong>CROPPING AND TREATMENTS</strong></p>
<P><strong>Continuous wheat:</strong> Wheat grown every year since autumn 1843, except when parts of the experiment were fallowed to control weeds. The experiment was divided into 10 sections in 1968; yields shown are mean of Sections 1 and 9 (two of the five sections currently in &quot;continuous&quot; wheat).<br />
<strong>1st wheat in rotation:</strong> Some parts of the experiment have been in crop rotations since 1968. Yields are from the 1st wheat in rotation; currently oats, maize, wheat, wheat, wheat (sections 2-5 and 7).<br />
<strong>Unfertilized - Nil</strong>: No fertilizer or manure applied since 1852.<br />
<strong>FYM:</strong> Farmyard manure - 35 t ha <sup>-1</sup> of Farmyard Manure applied each year since 1843.<br />
<strong>PK+144kgN</strong>: PK +144kgN ha <sup>-1</sup> applied each year since 1852.<br />
<strong>FYM+96kgN (+144kgN since 2005):</strong> 35 t ha <sup>-1</sup> FYM since 1885 plus 96kgN ha <sup>-1</sup> since 1968 (plus 144 kgN ha <sup>-1</sup> since 2005).<br /><strong>Greatest yield  NPK plots</strong>: 
Fertilizer treatment (NPK) giving highest yield each year (max. 288kgN ha <sup>-1</sup>). Since 2001 greatest yields have been selected from plots 8,9,12,15-18.<br />
</p>
<P><strong>AGRONOMIC PRACTICES</strong></p>
<P><strong>Fallowing:</strong> Between 1926 and 1967 the experiment was divided into five sections which were bare fallowed sequentially one year in five to control weeds. Wheat was grown in the other four years. <br />
<strong>Liming:</strong> Lime (calcium carbonate, often referred to as chalk) has been applied since the 1950s to maintain soil pH at a level which does not limit yield.<br /><strong>Herbicides:</strong> Herbicides were introduced in 1964; previously weeds were controlled by hand-hoeing or by fallowing and cultivation.<br /><strong>Modern cultivars:</strong> Modern short-strawed, high-yielding cultivars since 1968.<br />
<strong>Fungicides:</strong> spring and summer fungicides as necessary since 1978.</p>
