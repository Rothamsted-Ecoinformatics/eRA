There have been 53 species recorded in total since 1991 [<a href="<?php echo $metadata ; ?>Weeds list from BK WEEDS_SUM.pdf">species  list 1991-2018</a>] but on average there are 31 species found per year (with a  maximum of 38 in 1993 &amp; 1995 and a minimum of 26 in 2009) and there has  been a gradual slight decline over the period. The long dataset allows general,  overall trends to be observed and weed species are seen to differ greatly in  their response to a given set of conditions, some declining, some increasing  and others fluctuating - for example a decline in <em>Papaver rhoeas</em> between 1999 and 2003, and subsequent recovery (for  which there is no obvious explanation). Of the species currently recorded  annually ten species are locally common on many plots: Blackgrass (<em>Alopecurus myosuroides</em>), field poppy (<em>Papaver rhoeas</em>), common vetch (<em>Vicia sativa</em>),  parsley-piert (<em>Aphanes arvensis</em>), scentless mayweed (<em>Tripleurospermum inodorum</em>), shepherd's needle (<em>Scandix pectin-veneris</em>),  chickweed (<em>Stellaria media</em>), venus's looking glass (<em>Legousia hybrida</em>), creeping thistle (<em>Cirsium arvense</em>) and black medic (<em>Medicago lupulina</em>).  A principal  components analysis of the 1991-2002 survey data for 15 species showed clearly  the influence of inorganic N fertiliser levels on the frequency of individual  species. The frequency of one species (common chickweed (<em>Stellaria media</em>)) was greatly favoured by  increasing amounts of nitrogen fertiliser from 0 to 288 kg N ha-1, others were  strongly disadvantaged (e.g., black medic (<em>Medicago lupulina</em>) and  horsetail (<em>Equisetum arvense</em>)), some were  slightly disadvantaged (e.g., common vetch (<em>Vicia sativa</em>) and parsley-piert (<em>Aphanes arvensis</em>)), and some showed  little response to differing N rates (e.g., blackgrass (<em>A. myosuroides</em>) and  poppy (<em>P. rhoeas</em>)) (Moss  <em>et. al</em> 2004).</p>
<p>Corn cleavers (<em>Galium tricornutum</em>) is one of the rarest plants in the UK and this occurence on Broadbalk is the last known site in the UK. Numbers have increased from under five individuals in the 1990's to over 450 in 2011 and this has been a consequence of our active management strategy.</p>
