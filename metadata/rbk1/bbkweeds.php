<H1>Broadbalk Weeds</H1>
<p>The Broadbalk experiment was started in 1843 to investigate the relative  importance of different plant nutrients (N, P, K, Na, Mg) on grain yield of  winter wheat. Weeds were controlled initially by hand hoeing and fallowing, but  since 1964, herbicides have been applied to the whole experiment with the  exception of Section 8. This is one of the few arable sites in the country  where herbicides have never been applied. Weed surveys have been carried out annually  in two phases, the first between 1933-1979 and the second from 1991-present day. There are earlier records too, with the first plot-by-plot surveys of weed species  done in 1869  recording the  presence of 23 species in stubble in September. Since then, approximately 130  weed species have been recorded on Broadbalk Section 8, but many of these only  occur sporadically and about 30 of these are currently recorded annually (<a href="<?php echo $metadata ; ?>Complete list of weed species from Broadbalk Survey data 1869-2013.docx">Species list 1869-2018</a>). This  site also provides an invaluable reserve for seven nationally rare or uncommon  species including corn cleavers (<em>Galium tricornutum</em>), corn buttercup (<em>Ranunculus arvensis</em>), shepherd&rsquo;s needle (<em>Scandix pecten-veneris</em>) and  prickly poppy (<em>Papaver argemone</em>).  This is now the only location in the UK where corn cleavers   is known to occur naturally (see below). This resource and  associated data enables various weed investigations including weed population ecology, studies on the effects of  fallowing on the weed seed bank, seed dormancy and persistence, agroecology,  and population dynamics of individual weed species. Recently, molecular  approaches have been used to study the genetic diversity of weeds found on  Section 8, this rare herbicide-free arable plot.</p>
<p>Section 8 was fallowed in 2015 and 2016 so there is no data for these years. </p>
<h2><strong>Current Survey 1991 to present</strong> </h2>
<p>The current  weed survey was started on Section 8 in 1991 and has  continued annually ever since - although not on years when that section is in  fallow i.e. 1994, 2001, 2008, 2015 & 2016. It was fallowed in 2015 and 2016 in order to reduce an infestation of <em>Rumex obtusifolius</em> in particular (see table below). Section 8 (called section VA 1958-1967) was created in 1968 when the experiment was divided into its present layout (<a href="<?php echo $metadata ; ?>broadbalkplantoday.pdf">Broadbalk plan today</a>). The current assessment method records the  presence of individual weed species in 25 random quadrats (0.1m2) per plot. Each  year all 18 plots  are surveyed meaning 450 quadrats are assessed  per year, usually in June. Frequencies refer to the total number of quadrats in  which a weed is recorded, the maximum being 25 per plot and 450 across all  plots in the section. This method is more appropriate for detecting long-term  trends in weed frequencies and population differences between plots than the  earlier surveys (below) and provides a comprehensive set of 20 years of data for weed studies. </p>
