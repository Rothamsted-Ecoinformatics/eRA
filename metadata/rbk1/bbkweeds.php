<h2><strong>Earlier surveys 1933-1967 and 1968-1979</strong> </h2>
<p>  Annual surveys were conducted from 1933 to 1979. Originally, there were no sections on Broadbalk, just long strips the length of the whole field (<a href="<?php echo $metadata ; ?>Broadbalkplan1852-1925.pdf">Broadbalk plan 1852-1926</a>). In 1926 the field was divided into 5 sections (I-V) (<a href="<?php echo $metadata ; ?>Broadbalkplan1926-1967.pdf">Broadbalk plan 1926</a>). The whole field length was sampled for weeds as no herbicide weed control took place, just fallowing every five years. This gave all plot and section combinations (between 90 and 129 plots per year). After 1968, it was divided in to ten sections (<a href="<?php echo $metadata ; ?>broadbalkplantoday.jpg" target="_blank">Broadbalk plan today</a>) and again the whole field was surveyed each year - enabling comparison of weeds with and without herbicides. This appears  to be an excellent resource of data, however, it has not been widely used, principally because of the inconsistent frequency  categories  used. Neither were quadrats used, rather the assessor walked in a zig-zag pattern along the plot noting all weeds within 45cm of the plot boundary. Consequently  there  is a limitation on the interpretation of the data for ecological studies. The codes  used to indicate species presence and abundance (termed STATE  in the database) include the following:</p>
