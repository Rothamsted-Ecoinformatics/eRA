<p><strong>The following datasets are available: <br />
</strong><br />
  <strong>BBKWEEDS_FAL</strong> for 1933-67 (FAL indicating fallow) - herbicides applied  from 1964 to all sections except VA (which became section 8 in 1968). Data for all sections and all plots.<br />
  <br />
<strong>BBKWEEDS_ROT</strong> for 1968-79 (ROT indicating rotation with other crops, though not on section 8 which is rotated only with fallow):  - herbicides applied throughout to all sections except 8. Data  for all sections and all plots.</p>
<p>During these years, surveys were conducted twice  yearly, usually in May and August (sometimes as early as April or as late as September). 
  Supplementary surveys were done for special purposes such as for blackgrass which is not obvious in the early season surveys and become more obvious in the summer when flowering. It is recommended that you extract both STATE and  SPEC_REMARK since the presence of a species may be indicated by a remark such  as 'patch' even though there is no code for the state. In view of the large  number of null values it is probably best to tick the checkbox for STATE and  exclude 'null' and '-', meaning none. In these datasets, dates are termed START  DAY rather than year, as there are two surveys each year.<br />
  <br />
  There were 114 species names recorded during  1933-1967 [<a href="<?php echo $metadata ; ?>Weeds on section 8 Broadbalk 1933-1967.docx">species list 1933-1967</a>] and 113 species names during the 1968-79 surveys [<a href="<?php echo $metadata ; ?>Weeds list from DET BBKWEEDS_ROT.docx">species list 1968-79</a>].</p>
<p><strong>Earlier surveys - data not included in e-RA:</strong></p>
<p>Originally, there were no sections on Broadbalk, just long strips the length of the whole field (<a href="<?php echo $metadata ; ?>Broadbalkplan1852-1925.pdf">Broadbalk plan 1852-1926</a>). The first plot-by-plot list of weed species was done in 1869 and recorded the presence of 25 species in stubble in September [<a href="<?php echo $metadata ; ?>Weed list on Broadbalk 1869.docx">species list 1869</a>] (Thurston, 1969). Hand weeding and hoeing was practiced but due to the shortage of labour during the 1914-18 war the field became very weedy. Between 1926-1929 the field was fallowed to eliminate weeds, three out of five sections fallowed for 2 years running (<a href="<?php echo $metadata ; ?>Broadbalkcropping1926-1967.pdf">Broadbalk Cropping 1926-1967)</a>. From 1931 a regular cycle of fallowing 1 year in five was introduced and the effect of fallow on weed seeds was studied (Brenchley &amp; Warington 1930). Routine plot-by-plot surveys were started in 1930 and made twice yearly; first in May after spring germinating weeds are large enough to identify without trampling the crop and secondly, after the crop is cut and harvested which showed late germinating species. </p>
